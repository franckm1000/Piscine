<p>Apr&egrave;s le ferraillage du radier de la<em><strong> Piscine</strong></em>, c&rsquo;&eacute;tait le moment de couler le b&eacute;ton pour le radier. Je ne sais pas si le terme &quot;coulage&quot; est bien appropri&eacute; pour la chose mais bon je trouve que c'est assez explicite pour cette construction. J&rsquo;ai calcul&eacute; &agrave; 14m3 les besoins en b&eacute;ton pour la dalle (11x6x0,2=13,2). Pour ce faire j&rsquo;ai command&eacute; deux toupies, une de 8m3 et une de 6m3.


<p>Un des deux camions &eacute;tait ce qu&rsquo;on appelle un PUMI, camion toupie muni d&rsquo;une pompe de relevage car la <em><strong>piscine</strong></em> est difficilement accessible et le camion &eacute;tait situ&eacute; &agrave; 25 m&egrave;tres (pour ce genre de toupie comptez 500&euro; de plus). Pr&eacute;f&eacute;rez la solution de la toupie plut&ocirc;t que celle de la b&eacute;tonni&egrave;re pour cette partie de la construction. Renseignez vous aupr&egrave;s de la soci&eacute;t&eacute; pour le type de b&eacute;ton (voir cette petite section&nbsp;: <a href="typebeton.php" title="Choix du Béton">choix b&eacute;ton</a>)</p>
<p>J&rsquo;ai fais appel &agrave; un ma&ccedil;on professionnel pour m&rsquo;aider et j&rsquo;ai bien fait&nbsp;! En effet, si pour un fond plat le b&eacute;ton s&rsquo;&eacute;tale facilement, pour une pointe de diamant les choses sont diff&eacute;rentes. Premi&egrave;rement, du fait de la pente il faut demander du b&eacute;ton assez consistant et pas trop liquide. L&rsquo;inconv&eacute;nient est que du coup celui-ci &laquo;&nbsp;tire&nbsp;&raquo; plus vite.</p>
<p> On a vu dans la partie ferraillage<em><strong> Piscine </strong></em>que l&rsquo;on avait coffr&eacute; le tour du radier en indiquant sur les planches le niveau fini de la<em><strong> piscine</strong></em>. On commence donc par couler le tour de la pointe de diamant jusqu&rsquo;au niveau trac&eacute.</p>
<p align="center"><img src="images/coulage1.jpg" alt="Construire une Piscine" name="Construire une Piscine" style="width:600px;" /></p>

<p> On commence par faire couler le b&eacute;ton sur le tour de la pointe en le faisant descendre depuis le haut sur environ un m&egrave;tre. Sur la gauche vers la fl&egrave;che rouge, on voit que l&rsquo;on a d&eacute;j&agrave; aplani l&agrave; o&ugrave; il y aura les blocs. On a ainsi aplani sur 50cm de large tout autour de la pointe de diamant. A ce moment, on est affair&eacute; &agrave; bien faire ce pourtour &agrave; niveau pour faciliter la pose des blocs &agrave; bancher.</p>

<p align="center"><img src="images/coulage2.jpg" alt="Construire une Piscine" name="Construire une Piscine" style="width:600px;" /></p>
<p>&laquo;&nbsp;Le haut&nbsp;&raquo; de la pointe est pratiquement termin&eacute;. On voit la personne en train d&rsquo;&eacute;taler le b&eacute;ton &agrave; l&rsquo;int&eacute;rieur de la pointe. A ce moment, nous allons commencer &agrave; faire l&rsquo;int&eacute;rieur de la pointe en prenant des r&acirc;teaux et en tirant le b&eacute;ton de haut en bas.</p>
