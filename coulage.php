<p>Sur ce petit sch&eacute;ma, on voit la pointe de diamant. Les points en rouge repr&eacute;sentent les piquets (fers de 8) que l&rsquo;on a plant&eacute;s dans le sol pour nous guider dans la r&eacute;alisation de l&rsquo;int&eacute;rieur de la <a href="index.php" title="piscine">piscine</a>. Nous n&rsquo;avons pas mis de planches &agrave; l&rsquo;int&eacute;rieur pour mat&eacute;rialiser les &laquo;&nbsp;trap&egrave;zes&nbsp;&raquo;. Apr&egrave;s avoir tir&eacute; le b&eacute;ton avec le r&acirc;teau, on tape sur le b&eacute;ton avec une grosse taloche en bois pour aplatir le b&eacute;ton et on glisse la taloche de haut en bas au niveau des lignes rouges pour marquer les s&eacute;parations entre les quatre trap&egrave;zes de la pointe.</p>
<p align="center"><img src="images/coulage4.jpg" alt="Construire une Piscine" name="Construire une Piscine" style="width:600px;" /></p>
<p>La partie plate du radier nous prendra &agrave; peine 30 minutes&nbsp;! On voit sur cette photo le radier termin&eacute; avec les &laquo;&nbsp;semelles de reprise&nbsp;&raquo; qui ressortent du sol et qui rentreront dans les alv&eacute;oles des blocs &agrave; bancher.
<p>Remarque&nbsp;1: La &laquo;&nbsp;finition&nbsp;&raquo; du radier de la piscine est loin d&rsquo;&ecirc;tre parfaite notamment dans la pointe de diamant. Au contraire, l&rsquo;aspect rugueux est m&ecirc;me meilleur dans l&rsquo;attente d&rsquo;une chape de finition. Certaines personnes r&eacute;alisent un radier avec un aspect &laquo;&nbsp;fini&nbsp;&raquo;, c'est-&agrave;-dire bien lisse et &agrave; niveau en tout point. Cela permet d&rsquo;&eacute;viter la r&eacute;alisation d&rsquo;une chape de finition. Mais dans ce cas, il faut pr&eacute;voir un b&eacute;ton hydrofuge (&eacute;tanche) si vous mettez un carrelage. En outre cela est tr&egrave;s difficile dans le cas d&rsquo;une pointe de diamant, donc valable uniquement dans le cas d&rsquo;un fond plat.</p>
<p>Remarque 2 : Les semelles de reprise d&eacute;passent d&rsquo;environ 30cm du radier. Certaines personnes
 mettent des semelles de reprise de la hauteur finie des murs (1,2m pour nous). Cela n&rsquo;est pas obligatoire on peut tr&egrave;s bien une fois les murs mont&eacute;s remettre dans chaque alv&eacute;ole un fer vertical.
               

<div class="note">BETONNIERE OU PUMI? :
<p>D&eacute;j&agrave;, commen&ccedil;ons par casser un mythe de la construction. Il n'est pas &quot;oblig&eacute;&quot; de couler une dalle en une fois. Effectivement, c'est mieux mais on peut le faire en plusieurs fois pour peu qu'on fasse bien les reprises (humidification des reprises, utilisation d'une accroche type latex). Regardez les ouvrages d'art (type pont...) ils ne se sont pas fait en une fois.</p>
<p>Si le camion toupie peut se mettre &quot;&agrave; cul&quot; de votre chantier, n'h&eacute;sitez pas et prenez la toupie vous gagnerez un temps consid&eacute;rable pour un co&ucirc;t gu&egrave;re sup&eacute;rieur &agrave; la b&eacute;tonni&egrave;re.</p>
<p>Si (comme moi) le camion doit rester &eacute;loign&eacute; du chantier vous devrez faire appel &agrave; un camion PUMI (c'est &agrave; dire muni d'une pompe pouvant envoyer le b&eacute;ton &agrave; plus de 20 m&egrave;tres).</p>
<p>Pour moi, par exemple il me fallait 14m3 de b&eacute;ton. Voil&agrave; comment la soci&eacute;t&eacute; &agrave; qui j'ai fais appel a proc&eacute;d&eacute;e : on a prit le premier camion (muni de la pompe) et on a d&eacute;vers&eacute; les 6m3 pr&eacute;sents dans ce camion. Ensuite, on a &quot;transvas&eacute;&quot; les 8m3 du deuxi&egrave;me camion dans le premier (car le deuxi&egrave;me n'ayant pas de pompe il a fallu tout d&eacute;verser dans le premier) et enfin envoy&eacute; ces 8m3 sur le chantier. Le co&ucirc;t est alors assez &eacute;lev&eacute; (allez voir dans la partie <a href="BudgetPiscine.php" title="Budget de la Piscine">budget</a>)</p>
<p>Dans ce cas, prendre la b&eacute;tonni&egrave;re aurait revenu effectivement moins cher. Maintenant, faire 14m3 de b&eacute;ton &agrave; la main n'est tout de m&ecirc;me pas une mince affaire. A vous de voir en fonction de votre budget et des mains disponibles !</p>

