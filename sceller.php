<p>On a vu dans la section pr&eacute;c&eacute;dente que dans le cas d&rsquo;une filtration classique, une partie des &eacute;l&eacute;ments de la filtration (skimmers, refoulements, bonde de fond, prise balai et projecteurs) devaient &ecirc;tre int&eacute;gr&eacute;s &agrave; la structure d&rsquo;o&ugrave; leurs noms d&rsquo;&eacute;l&eacute;ments &agrave; sceller.</p>
<p>On va montrer dans cette section construction piscine de quelle fa&ccedil;on on a proc&eacute;d&eacute;. J&rsquo;indique ici la m&eacute;thode que j&rsquo;ai utilis&eacute;e, il en existe d&rsquo;autres et peut &ecirc;tre s&ucirc;rement des meilleures mais celle que j&rsquo;ai utilis&eacute;e va tr&egrave;s bien&nbsp;!</p>

<h2>A. Les skimmers de la Piscine.</h2>

<p>Comme on l&rsquo;a vu, ils vont &ecirc;tre install&eacute;s sur la largeur en face de l&rsquo;escalier (face aux vents dominant, vent du sud chez moi). Comme la piscine fait 10x5m, j&rsquo;ai d&eacute;cid&eacute; d&rsquo;en installer deux. Il faut donc les mettre &agrave; &eacute;gale distance des c&ocirc;t&eacute;s. J&rsquo;ai d&eacute;cid&eacute; de les mettre &agrave; un m&egrave;tre de chaque c&ocirc;t&eacute; de la largeur. Il faut bien faire attention &agrave; les mettre &agrave; &eacute;gale distance, cela serait tr&egrave;s voyant si ils &eacute;taient mal r&eacute;partis de chaque c&ocirc;t&eacute;.</p>

<p>Ces petites choses peuvent en effet vous g&acirc;cher la vie plus tard : c'est tr&egrave;s d&eacute;sagr&eacute;able de se baigner dans sa piscine en voyant sans arr&ecirc;t ces skimmers mal distanc&eacute;s dans la piscine.</p>
<img src="images/sceller1.jpg" alt="Construire une Piscine" title="Construire une Piscine" style="width:600px;" />
<p>Emplacement des futurs skimmers</p>
<p>Les skimmers doivent &ecirc;tre plac&eacute;s le plus haut possible, en effet c&rsquo;est eux qui d&eacute;terminent la hauteur d&rsquo;eau.</p>
<p>Faites en sorte que le haut du skimmer arrive &agrave; la limite de l'<a href="arase.php" title="arase de la piscine">arase</a> de la piscine. Une piscine avec une eau presque jusqu'en haut du mur est plus jolie.</p>
