<h2>B. Les Refoulements de la piscine.</h2>
<p>Ils sont au nombre de trois. Un au milieu de l&rsquo;escalier et un de chaque c&ocirc;t&eacute; de l&rsquo;escalier.</p>
<p>Pour installer vos refoulements, il faut soi utiliser une travers&eacute;e de paroi (celle-ci vous sert pour traverser les blocs &agrave; bancher), c&rsquo;est ce que j&rsquo;ai utilis&eacute; pour les deux refoulements sur le c&ocirc;t&eacute; de l&rsquo;escalier. Dans ce cas le refoulement est viss&eacute; &agrave; la travers&eacute;e de paroi.</p>
<img src="images/sceller20.jpg" alt="Construire une Piscine" title="Construire une Piscine" style="width:200px;" />
<p> Travers&eacute;e de paroi</p>
