<h2>D. La Bonde de Fond de la Piscine.</h2>
<p>Elle est scell&eacute;e dans le radier, il faut donc l&rsquo;installer en m&ecirc;me temps que le ferraillage. Choisissez l&rsquo;endroit ou vous souhaitez la mettre, calculez la hauteur approximative ou elle doit &ecirc;tre (en tenant compte de l&rsquo;&eacute;paisseur du radier) puis scellez avec du mortier bien &agrave; niveau.</p> 
<p>Ensuite, tirez un tuyau en PVC souple depuis la bonde de fond en faisant une petite tranch&eacute;e ou en le faisant passer comme moi entre mes deux ferraillages.</p>
<p>Allez voir la partie <a href="ferraillage.php" title="ferraillage du Radier de la Piscine" target="_blank">ferraillage</a>, il y a des photos avec la bonde de fond.</p>
<h2>E. Les Projecteurs de la Piscine.</h2>
<p>Vu la grandeur de la piscine, j&rsquo;ai d&eacute;cid&eacute; d&rsquo;en installer deux. Ils sont install&eacute;s sur une des deux longueurs. On l&rsquo;installe du c&ocirc;t&eacute; oppos&eacute; &agrave; la terrasse pour &eacute;viter de se faire &eacute;blouir. La m&eacute;thode de pose est la m&ecirc;me que pour les refoulements.</p> 
<p>Les projecteurs vont &ecirc;tre scell&eacute;s &agrave; environ 60cm du haut de la piscine. On pose les projecteurs en m&ecirc;me temps que l&rsquo;on monte les blocs&nbsp;: c'est-&agrave;-dire que une fois les quatre premi&egrave;res rang&eacute;es pos&eacute;es, on fait les r&eacute;servations et on scelle les projecteurs.</p>
