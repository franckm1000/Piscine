<p>Une fois que l&rsquo;on a mont&eacute; les hauteurs de blocs, on fait des r&eacute;servations &agrave; l&rsquo;endroit voulu comme sur la photo ci-dessus. Les skimmers ne seront plac&eacute;s qu&rsquo;apr&egrave;s avoir banch&eacute; les blocs.</p>
<p>On a vu qu&rsquo;on a deux fers de 8 par ligne de bloc, or si on regarde on voit bien que les fers de la derni&egrave;re rang&eacute;e vont g&ecirc;ner pour poser le skimmer. Il faut donc trouver un moyen pour contourner les skimmers de la <a href="index.php" title="piscine">piscine</a>. On pourrait d&eacute;j&agrave; penser par les couper tout simplement, mais la derni&egrave;re rang&eacute;e peut &ecirc;tre consid&eacute;r&eacute;e comme une sorte de ceinture p&eacute;riph&eacute;rique haute donc on le d&eacute;conseille (m&ecirc;me si certains semblent le faire sans que cela pose trop de soucis, mais l&agrave; je ne suis pas assez connaisseur pour r&eacute;pondre).</p>
<p>Trois solutions sont envisageables, passer les fers par-dessus le <a href="filtration1.php" title="filtration">skimmer</a>, par-dessous ou voir m&ecirc;me par derri&egrave;re. J&rsquo;ai choisi de les faire passer par dessous. Il faut donc une fois arriv&eacute; au skimmer tordre les fers pour leurs faire rejoindre la rang&eacute;e inf&eacute;rieure puis une fois le skimmer pass&eacute; les faire remonter. Cela n&rsquo;est pas aussi ais&eacute; qu&rsquo;il n&rsquo;y parait car il faut tordre deux fois les fers &agrave; moins de un m&egrave;tre d&rsquo;intervalle. Ci-dessous les photos pour les explications&nbsp;:</p>
