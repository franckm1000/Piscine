<img src="images/sceller5.jpg" alt="Construire une Piscine" title="Construire une Piscine" style="width:600px;" />
<p>1. On banche les blocs normalement</p>
<p>2. Une fois arriv&eacute; au niveau de la r&eacute;servation, on met le skimmer en place en le mettant bien &agrave; niveau et &agrave; la bonne hauteur.</p>
<p>3. On scelle autour du skimmer avec du ciment prompt (ciment &agrave; prise rapide).</p>
<p>J&rsquo;ai pr&eacute;vu de faire une arase &laquo;&nbsp;d&rsquo;environ&nbsp;&raquo; cinq centim&egrave;tres. J&rsquo;ai donc laiss&eacute; d&eacute;passer les skimmers en hauteur de 3 centim&egrave;tres , de cette fa&ccedil;on ils seront deux centim&egrave;tres en dessous des margelles (on a vu qu&rsquo;il fallait qu&rsquo;ils soient le plus haut possible).</p>
<img src="images/sceller6.jpg" alt="Construire une Piscine" title="Construire une Piscine" style="width:600px;" />
