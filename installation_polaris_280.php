<p>Cette section traite de l'installation d'un <b>robot piscine polaris</b> 280 pour une piscine.
 Le <b>robot piscine</b> hydraulique comme le polaris 280 est une excellente solution pour le nettoyage de votre pisine. Néanmoins, il nécessite l'installation d'un surpresseur dans le local technique et cela n'est pas forcément aisé. Mon beau frère souhaitant s'en équiper, je l'ai aidé à faire l'installation et j'en profite fonc pour partager cette expérience !</p>


<p>Un local technique traditionnel avec filtre à sable est quasiment toujours architecturé de la même façon, à savoir: trois arrivées d'aspiration (skimmer, bonde de fond, prise balai) qui partent à la pompe, ensuite au filtre et qui enfin repartent aux refoulements vers la piscine.
<p style="text-align:center"><img src="images/polaris_1.jpg" width="70%"  /></p>
<p>Pour installer le surpresseur qui va servir à faire marcher le robot, il va falloir détourner la prise balai. En effet, le robot polaris est branché sur la prise balai dans la piscine, il va donc falloir brancher le surpresseur du robot polaris sur la prise balai. </p>
<p>On sait également que le robot polaris fonctionne en surpression, c'est à dire que l'eau arrive dans le robot par pression et non par aspiration. Il va donc falloir brancher le surpresseur pour qu'il &quot;envoie&quot; de l'eau dans la prise balai. Cette eau, il ira la chercher après le filtre sur les refoulements. Comme ceci:</p>
<p style="text-align:center"><img src="images/polaris_2.jpg" width="600" height="750" /></p>
