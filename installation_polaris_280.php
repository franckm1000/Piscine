<p>On voit bien sur ce schéma que le surpresseur du robot aspire l'eau qui part aux refoulements pour la renvoyer dans la prise balai et ainsi dans le robot polaris qui pourra fonctionner. On notera les deux vannes permettant de couper le surpresseur et ainsi de pouvoir utiliser à nouveau la prise balai avec la pompe de filtration. En mode normal, les deux vannes du surpresseur sont ouvertes alors que la vanne de la prise balai qui va à la pompe principale est fermée. Voyons maintenant plus en détail l'installation. </p>
<p>Le surpresseur installé est un surpresseur Starite, très petit et très peu bruyant. En outre, il fonctionne avec une tuyauterie de diamètre 32. Ainsi, on va se brancher sur untuyauterie de 50 (la tuyauterie de la pompe) avec du diamètre 32. Pour cela, on va utiliser des colliers de prise en charge comme celui-ci:</p>
<p style="text-align:center"><img src="images/IMG_4694.JPG"  alt="Polaris 280" width="512" height="384" /></p>
<p>On va donc commencer par installer les deux colliers de prise en charge. Pour cela, à l'aide d'une scie cloche, on perce deux trous (un sur les refoulements et l'autre à l'arrivée de la prise balai avant la vanne :</p>
<p style="text-align:center"><img src="images/IMG_4696.JPG" width="512" height="384" alt="polaris 280" /></p>
<p style="text-align:center"><img src="images/IMG_4692.JPG" width="512" height="384" alt="polaris 280" /></p>
<p>Une fois le trou percé, on installe le collier de prise en charge. Une fois le collier installé, celui ci possède un pas de vis de 50. On va donc visser un raccord union sur chacun des colliers :</p>
<p style="text-align:center"><img src="images/IMG_4691.JPG" width="512" height="384" alt="installation polaris" /></p>
<p>Une fois les deux raccords unions installés, on pose la tuyauterie depuis le collier de départ (situé aux refoulements) que l'on emmène vers la pompe. Ensuite, on repartira de la pompe pour retourner vers le deuxième collier de prise en charge qui se trouve lui vers l'arrivée de la prise balai. Bien sûr, pour ces collages, vous devrez certainement utiliser des coudes voir des manchons:</p>
<p style="text-align:center"><img src="images/IMG_4693.JPG" width="512" height="384" alt="polaris 280" /></p>
<p style="text-align:center">On voit sur la photo le raccord union en haut que l'on va relier à la pompe</p>
<p style="text-align:center"><img src="images/IMG_4695.JPG" width="512" height="384" alt="surpresseur polaris" /></p>
<p style="text-align:center">On a relié le premier collier de prise en charge avec la pompe. A noter la vanne.</p>
<p>On voit également que pour relier à la pompe la tuyauterie, on utilise également des raccords union. En tout, on aura donc besoin de 4 raccords unions et de deux colliers de prise en charge.</p>
<p style="text-align:center"><img src="images/IMG_4697.JPG" width="512" height="384" alt="polaris 280" /></p>
<p style="text-align:center">Sur cette photo, on voit que l'on est reparti de la pompe pour se diriger vers l'arrivée de la prise balai.</p>
<p style="text-align:center"><img src="images/IMG_4702.JPG" width="512" height="384" alt="polaris 280" /></p>
<p style="text-align:center">On voit ici l'installation terminée: le surpresseur est en place et est prêt à fonctionner.</p>
<p>Il faudra encore bien sûr relier le surpresseur à un tableau électrique dédié. Ce tableau électrique possède une horloge vous permettant ainsi de programmer le fonctionnement de votre robot. A noter:</p>
<ul>
  <li>Il faut relier le surpresseur au coffret électrique dédié</li>
  <li>Ce coffret doit être relié lui même à l'arrivée générale après le différentiel sur un disjoncteur dédié (16A suffisent)</li>
  <li>Le coffret doit également être relié au coffret de filtration de la pompe principale: sur le coffret de votre filtration principale, vous devez avoir ce qu'on appelle des bornes d'asservissement (généralement notées 13/14 ou 7/8). Vous devez brancher le coffret dédié au surpresseur sur ces deux bornes pour asservir votre surpresseur: ainsi, si votre pompe principale s'arrête, cela oblige également le surpresseur à s'arrêter et à éviter ainsi qu'il s'abime.</li>
  </ul>
