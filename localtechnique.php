<h2>VI. Le Robot Polaris</h2>
<p>Le &laquo;Robot&raquo; est un appareil servant &agrave; nettoyer le fond de la piscine et &eacute;ventuellement les parois. On peut distinguer plusieurs types de robots :</p>
<h3>1. Les robots &eacute;lectriques</h3>
<p>Autonomes, fonctionnant sur batterie ou secteur, ils nettoient le bassin au fond et sur les parois. La qualit&eacute; de nettoyage est le grand atout de ce type d'appareil. L'&eacute;lectronique embarqu&eacute;e leur permet d'avoir &eacute;galement beaucoup d'options (plusieurs programmes de nettoyages...).&nbsp;</p>
<p>Mais je leur trouve deux d&eacute;fauts : un mineur, le prix (environ 1500&euro; pour un moyen de gamme) et un d&eacute;faut majeur, leur fiabilit&eacute;. Quoi qu'on en dise, il n'est pas rare qu'un robot pr&eacute;sente des d&eacute;faillances au bout de cinq ans d'utilisation.</p>
<h3>2. Les robots hydrauliques</h3>
<p>Ils fonctionnent gr&acirc;ce au courant de l'eau et sont en g&eacute;n&eacute;ral branch&eacute;s sur la prise balai. On distingue alors deux types : les syst&egrave;mes par aspiration (le robot est branch&eacute; sur la prise balai, l'eau est aspir&eacute;e&nbsp;via le robot) et les syst&egrave;mes par surpression (type Polaris). Ces robots ne n&eacute;c&eacute;ssitent pas de courant &eacute;lectrique.&nbsp;</p>
<p>Leur avantage est leur prix (environ 800&euro; sur Internet aujourd'hui pour un Polaris 280 avec pompe et tableau &eacute;lectrique) et leur solidit&eacute; (plus besoin de faire la publicit&eacute; sur la robustesse du&nbsp;Polaris).Leur inconv&eacute;nient : l'installation. Par exemple, pour le Polaris, il faut installer une deuxi&egrave;me pompe dans le local technique (surpresseur) et un tableau &eacute;lectrique associ&eacute;.</p>
<p>Personnellement, j'ai donc opt&eacute; pour le Polaris 280. J'ai donc fais l'acquisition du Robot (branch&eacute; sur la prise Balai), du surpresseur (pompe de 1Cv) et d'un tableau &eacute;lectrique. Le surpresseur est branch&eacute; sur la prise balai. Voici le principe :</p>
<p>Le surpresseur aspire de l'eau &agrave; la sortie du filtre (sur le tuyau des refoulements), il renvoie cette eau dans la prise balai. Le robot est branch&eacute; sur la prise balai et alors le courant cr&eacute;e fait bouger le robot. Dans le robot, un syst&egrave;me fait que les d&eacute;bris sont collectionn&eacute;s et stock&eacute;s dans un filtre.</p>
<p>L'eau est r&eacute;cup&eacute;r&eacute;e apr&egrave;s le filtre, passe par le surpresseur et est ensuite renvoy&eacute;e via la prise balai.</p>
