<h2>I.Pr&eacute;paration et r&eacute;alisation de la dalle du Local Technique.</h2>
<p>J&rsquo;ai trouv&eacute; une cabane dans le commerce de dimension 2,4m x 1,2m, elle ira juste bien pour ce que je veux y mettre. Cette cabane reposera sur une dalle en b&eacute;ton. On va donc faire remonter les tuyaux dans la dalle, de cette fa&ccedil;on&nbsp;:</p>
