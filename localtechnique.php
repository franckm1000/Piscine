<p>1. Un bouton : Marche-Arr&ecirc;t-Asservissement pompe principale (auto)</p>
<p>2. Une horloge de programmation</p>
<p>3. Un disjoncteur</p>
<div class="note">
<p>Remarque 1: Sur le disjoncteur, il y a une petite molette o&ugrave; l'on peut r&egrave;gler l'intensit&eacute;. Si vous voyez que votre tableau coupe le courant apr&egrave;s un petit moment d'utilisation du surpresseur c'est qu'il faut peut &ecirc;tre augmenter l'intensit&eacute; maximale &agrave; l'aide de la molette.</p>
<p>Remarque 2: Il faut bien brancher le surpresseur en surpression et non en aspiration (il doit refouler de l'eau dans la piscine et non aspirer l'eau de la piscine). Il faut donc le brancher &agrave; l'inverse de la pompe de la filtration. Je l'avais branch&eacute; &agrave; l'envers, il a fallu tou red&eacute;monter et refaire.</p>
