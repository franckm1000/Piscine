<h2>IV.Montage de l&rsquo;&eacute;lectrolyse au sel</h2>

<p>J&rsquo;ai choisi comme syst&egrave;me de d&eacute;sinfection l&rsquo;&eacute;lectrolyse au sel surtout pour sa facilit&eacute; d&rsquo;utilisation, en effet plus besoin d&rsquo;utiliser des galets de chlore la d&eacute;sinfection se fait automatiquement.&nbsp;</p>
<p>Cette &eacute;lectrolyse est compos&eacute;e d&rsquo;une cellule branch&eacute;e sur le circuit de filtration apr&egrave;s le filtre et d&rsquo;un tableau &eacute;lectrique. Ce tableau &eacute;lectrique est reli&eacute; au tableau &eacute;lectrique de la pompe, de cette fa&ccedil;on l&rsquo;&eacute;lectrolyse fonctionne en m&ecirc;me temps que la filtration.</p>
<div><img src="images/local6.jpg" alt="Construire Piscine" style="width:600px;" /></div>
<p>La cellule pour l&rsquo;&eacute;lectrolyse est plac&eacute;e apr&egrave;s le filtre et apr&egrave;s un &eacute;ventuel chauffage. Elle est mont&eacute;e en by-pass, c'est-&agrave;-dire d&eacute;vi&eacute;e du circuit principal. De chaque c&ocirc;t&eacute; de la cellule on met des vannes, de cette sorte on peut se passer de l&rsquo;&eacute;lectrolyse.</p>
<p>Petite pr&eacute;cision concernant l&rsquo;&eacute;lectrolyse&nbsp;: celle-ci contrairement &agrave; une id&eacute;e re&ccedil;ue produit bien du chlore (et m&ecirc;me en quantit&eacute; importante si l'on peut dire). Mais si le syst&egrave;me &agrave; &eacute;lectrolyse ne sent pas le chlore, en voici la raison :</p>
<p>En passant dans la cellule, le sel (pr&eacute;sent dans l'eau) rentre en r&eacute;action avec l'&eacute;lectrode et &agrave; ce moment une r&eacute;action &eacute;lectrochimique d'oxydation du sel s&rsquo;op&egrave;re. Il est converti en&nbsp;acide hypochloreux (composant actif de l'eau de javel). Ce chlore d&eacute;truit les bact&eacute;ries et micro organismes. Mais le plus important est que en retourant dans la piscine par les refoulements, ce chlore se reconvertira naturellement en sel sous l'effet des UV du soleil.</p>
<p>Donc l'&eacute;lectrolyse produit du chlore mais la diff&eacute;rence fondamentale avec un syst&egrave;me de galets est que la production de chlore est PERMANENTE. En effet, avec les galets, ceux ci se diffusent lentement mais il faut y adjoindre ce qu'on appelle un stabilisant (qui s'occupera en fait d'emp&ecirc;cher au chlore de dispara&icirc;tre sous l'effet des UV). C'est souvent &agrave; cause de cela qu'il y a des d&eacute;sagr&eacute;ments (odeur de chlore, yeux qui piquent...).</p>
<p>Avec l'&eacute;lectrolyse, beaucoup de chlore est produit mais beaucoup de chlore est &eacute;galement &eacute;limin&eacute; (pas de stabilisant). Au bout du compte, la d&eacute;sinfection est excellente et il n'y a aucun d&eacute;sagr&eacute;ments.</p>
<div><img style="width:600px;" alt="Local Piscine" src="images/local7.jpg" /></div>
<p>Tableau Electrique de l'&eacute;lectrolyse. En bas &agrave; gauche, le bouton Marche-Arr&ecirc;t.</p>
<p>En bas &agrave; droite, on peut r&egrave;gler la production de chlore (mini-moyen-maxi)</p>
<p>L'aiguille en haut permet de r&egrave;gler la quantit&eacute; de sel dans l'eau (mini de 4 g/l et maxi de 5g/l)</p>
<p>Elle poss&egrave;de une alarme d&eacute;bit, si l'eau ne passe plus dans la cellule, celle ci se met en  s&eacute;curit&eacute;.</p>
<p>Le voyant auto-nettoyage veut dire que la cellule est &agrave; inversion de polarit&eacute;. De temps en temps, elle fait un cycle d'inversion de polarit&eacute; qui lui permet de se nettoyer et &eacute;vite l'entartrage.</p>
<p>&nbsp;</p>
<h2>V.Montage du r&eacute;gulateur de Ph</h2>
<p>Le r&eacute;gulateur de PH doit servir pour r&eacute;guler automatiquement la teneur en PH de l&rsquo;eau de la piscine. Cette teneur en PH doit toujours se situer id&eacute;alement entre 7,2 et 7,6 (petit rappel&nbsp;: PH neutre &agrave; 7, de + en + acide en dessous de 7 jusqu&rsquo;&agrave; 0, de + en + basique au dessus de 7 jusqu&rsquo;&agrave; 14).</p>
<p>Pour cela, on peut diviser ce r&eacute;gulateur en trois parties&nbsp;:</p>
<p>1. Une sonde reli&eacute;e au circuit et qui calcule en temps r&eacute;el la teneur en PH de l&rsquo;eau</p>
<p>2. Un injecteur qui le cas &eacute;ch&eacute;ant va envoyer un liquide correcteur de PH dans le circuit d&rsquo;eau pour que l&rsquo;eau retrouve une teneur correcte en PH.</p>
<p>3. Un bidon de liquide correcteur de PH (+ ou -) reli&eacute; au syst&egrave;me.</p>
