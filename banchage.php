<p><span class="Style49">Le banchage est l&rsquo;op&eacute;ration  consistant &agrave; remplir les blocs de la Piscine de b&eacute;ton. On rappelle qu&rsquo;une fois les blocs  de la piscine mont&eacute;s, on a des poches verticales de 1,2m (dans mon cas, bien s&ucirc;r cela peut  varier suivant la hauteur de mur) &agrave; remplir de b&eacute;ton.</span></p>
<p align="left" class="Style49">On peut comme le radier  utiliser une toupie &agrave; b&eacute;ton mais l&agrave; il serait plut&ocirc;t conseill&eacute; d&rsquo;y faire &agrave; la  main. Premi&egrave;rement, vous pouvez couler les blocs en plusieurs fois pas de  probl&egrave;mes. Simplement, quand vous vous arr&ecirc;tez faites le au <b><u>milieu d&rsquo;un bloc</u></b>. Ensuite, la quantit&eacute; de b&eacute;ton &agrave; faire est moindre que pour le  radier de la piscine. Par exemple, dans mon cas on a 33m lin&eacute;aires de blocs d&rsquo;une hauteur de  1,2m. Un bloc fait 20cm de large mais avec 14cm de vide seulement donc on doit  faire en tout&nbsp;: 33x 1,2x 0,14 = 5,55m3 de b&eacute;ton ce qui est parfaitement faisable  &agrave; la main.</p>
<p align="left" class="Style49">Le b&eacute;ton doit &ecirc;tre un b&eacute;ton &laquo;&nbsp;standard&nbsp;&raquo; c'est-&agrave;-dire dos&eacute; &agrave; 350 kg/m3. Ainsi, sachez que pour 1m3 de b&eacute;ton il vous faut 10 sacs de ciment de 35 kilos. Pour le dosage, utilisez la r&egrave;gle du 1-2-3&nbsp;: Une pelle de ciment, deux de sable, trois de gravier (si vous commandez du paveur 0-20, mettez une pelle de  ciment pour 5 pelles de paveur).</p>
<p align="left" class="Style49">Dans votre b&eacute;tonni&egrave;re,  commencez par mettre de l&rsquo;eau puis cinq pelles de paveur puis une pelle de  ciment puis cinq pelles de paveur puis une pelle de ciment et ainsi de suite et  rajoutez un peu d&rsquo;eau de temps en temps jusqu&rsquo;&agrave; ce que la b&eacute;tonni&egrave;re soit  pleine. Vous verrez, au bout de cinq ou six b&eacute;tonni&egrave;res vous prendrez l&rsquo;habitude pour y faire&nbsp;!</p>

<p align="left" class="Style49">L&rsquo;id&eacute;al je pense pour y  faire est d&rsquo;&ecirc;tre quatre ou cinq personnes. Dans mon cas, on l&rsquo;a fait &agrave; cinq cela  va tr&egrave;s bien. Trois personnes est un minimum pour le faire. Une personne pour  faire le b&eacute;ton, une deuxi&egrave;me pour trimbaler la brouette et une personne pour  r&eacute;ceptionner le b&eacute;ton dans les blocs de la piscine.</p>
<p align="left" class="Style49">Attention, car du fait de  la pr&eacute;sence de deux fers horizontaux &agrave; chaque ligne, le b&eacute;ton peut avoir du mal  &agrave; descendre jusqu&rsquo;en bas. Il faut donc toujours <b><u>bien remuer le b&eacute;ton</u></b> pour &ecirc;tre sur qu&rsquo;il descende bien jusqu&rsquo;en bas. Je vous  d&eacute;conseille une aiguille vibrante, prenez simplement un bon fer de 10 et  touillez bien &agrave; chaque fois que vous coulez le b&eacute;ton dans les blocs. Soignez  particuli&egrave;rement au niveau des &eacute;l&eacute;ments &agrave; sceller car c&rsquo;est l&agrave; que peuvent se  cr&eacute;er des poches (notamment en dessous des projecteurs).</p>
