<h1><a name="coque"><u><b>1.Les Coques Piscine</b></u></a></h1>

<p>Une <em><strong>piscine</strong></em> en coque polyester monocoque est fabriqu&eacute;e &agrave; l'aide d'un moule ayant la forme du bassin(une multitudes de formes sont de ce fait possible). De la r&eacute;sine polyester(m&eacute;lang&eacute;e &agrave; de la fibre de verre) est projet&eacute;e dans le moule. Apr&egrave;s s&eacute;chage,on proc&egrave;de au d&eacute;moulage et alors la coque est pr&ecirc;te &agrave; &ecirc;tre pos&eacute;e.</p>

