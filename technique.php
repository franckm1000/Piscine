<a name="monobloc"></a><h1>3.<b><u>Les piscines b&eacute;ton monobloc</u></b></h1>
<p>Il existe plusieurs m&eacute;thodes pour construire des <em><strong>piscines</strong></em> en b&eacute;ton monobloc. Par exemple, une des techniques est celle du b&eacute;ton projet&eacute;. L&rsquo;ossature de la <em><strong>piscine</strong></em> est r&eacute;alis&eacute;e en treillis de fer, le b&eacute;ton est ensuite projet&eacute; sur le treillis &agrave; l&rsquo;aide d&rsquo;un canon. L&rsquo;ensemble forme ainsi une structure en b&eacute;ton arm&eacute; sur laquelle on peut poser le rev&ecirc;tement de son choix. On appelle aussi cela le &laquo;&nbsp;gunitage&nbsp;&raquo;</p>
<p>La qualit&eacute; de la construction est optimale mais la mise en &oelig;uvre d&rsquo;un tel bassin ne peut &ecirc;tre r&eacute;alis&eacute;e que par un professionnel et en aucun cas par un particulier comme moi&nbsp;!</p>
