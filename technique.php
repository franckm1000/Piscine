  <a name="bloc"></a><h1>7.<u><b>Construire sa Piscine en Blocs &agrave; bancher</b></u></h1>
  <p>Le bloc &agrave; bancher comme les syst&egrave;mes Desjoyaux ou Magiline s&rsquo;apparente &agrave; un syst&egrave;me de coffrage perdu. On dira que c&rsquo;est un syst&egrave;me g&eacute;n&eacute;rique de coffrage (!!!) car en g&eacute;n&eacute;ral moins cher &agrave; mettre en &oelig;uvre.</p>
<p>Un bloc &agrave; bancher se pr&eacute;sente comme un parpaing mais sa particularit&eacute; est qu&rsquo;il n&rsquo;a pas de fond (on peut passer sa main au travers). On les monte en les croisant, c'est-&agrave;-dire d&eacute;cal&eacute;s d&rsquo;une ligne sur l&rsquo;autre, de cette fa&ccedil;on on cr&eacute;e des poches verticales dans lesquelles on coulera le b&eacute;ton.</p>
<p>A chaque ligne, on pose deux fers de fa&ccedil;on horizontale sur tout le tour et un fer vertical dans chaque alv&eacute;ole. En outre, dans chaque alv&eacute;ole rentre un semelle de reprise du radier (voir partie <a href="ferraillage.php">Ferraillage radier</a>) qui assurera la liaison Radier-Mur. De cette fa&ccedil;on on obtient une construction en b&eacute;ton arm&eacute;.</p>
<p>Il existe deux tailles de blocs &agrave; bancher. Blocs de 20 de largeur (20x20x50) et blocs de 27 de largeur(27x20x50). Les blocs de 27 sont utilis&eacute;s uniquement pour les grandes piscines, jusqu&rsquo;&agrave; 10 m&egrave;tres de longueur on peut utiliser des blocs de 20cm de largeur. </p>
