<a name="coffrage"></a><h1>6.<b><u>Syst&egrave;mes de Piscine industriels avec coffrage perdu</u></b></h1>
<p>Ce syst&egrave;me de <em><strong>piscine</strong></em> est compos&eacute; de panneaux creux que l&rsquo;on assemble les uns apr&egrave;s les autres pour obtenir la piscine. Apr&egrave;s ferraillage, on coule le b&eacute;ton dans les coffrages en m&ecirc;me temps que l&rsquo;on coule le radier. Ainsi, les murs et le radier sont li&eacute;s en un bloc et on a une structure en b&eacute;ton arm&eacute;.</p>
<p>On parle de coffrage perdu car une fois le b&eacute;ton coul&eacute; on ne peut plus r&eacute;cup&eacute;rer le coffrage, diff&eacute;remment d&rsquo;un syst&egrave;me de coffrage en planches de bois par exemple o&ugrave; apr&egrave;s s&eacute;chage du b&eacute;ton on peut d&eacute;coffrer.</p>
<p>Cette fa&ccedil;on de proc&eacute;d&eacute; est tr&egrave;s r&eacute;pandue actuellement et les plus grands <em><strong>constructeurs de piscine</strong></em> l&rsquo;utilisent (<a href="http://www.desjoyaux.fr/" title="Lien Site Desjoyaux">Desjoyaux</a>, <a href="http://www.magiline.fr/" title="Lien Site Magiline">Magiline</a>).</p>
<p>Ce syst&egrave;me de construction est souvent vendu en Pr&ecirc;t &agrave; plonger ou en kit au choix. On peut en trouver &agrave; des prix tr&egrave;s raisonnables, simplicit&eacute; et rapidit&eacute; d&rsquo;installation sont ses grands avantages. N&eacute;anmoins, une restriction&nbsp;: l&rsquo;obligation de poser un Liner comme &eacute;tanch&eacute;it&eacute; pour la piscine.</p>
<p>Deux liens pour approfondir ces syst&egrave;mes&nbsp;:</p>
<p><a href="http://stefandh.skyblog.com/" target="_blank">http://stefandh.skyblog.com/</a>&nbsp;: Kit Magiline 8x4</p>
