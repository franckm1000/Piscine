<a name="kit"></a><h1>4.<b><u>Construire sa Piscine en kit panneaux</u></b></h1>
<p>Alors l&agrave;, au moins on a l&rsquo;embarras du choix&nbsp;! Si on veut r&eacute;sumer en gros, ce syst&egrave;me de construction de piscine est constitu&eacute; de panneaux (en acier galvanis&eacute;, inox&hellip;) que l&rsquo;on assemble les uns les autres, ils peuvent &ecirc;tre scell&eacute;s sur le radier par des jambes de force. Ensuite, on pose les &eacute;l&eacute;ments &agrave; sceller puis le liner. Il existe beaucoup de syst&egrave;mes diff&eacute;rents de kits piscine &agrave; des prix diff&eacute;rents.</p>
<p>L&rsquo;avantage de cette m&eacute;thode est sa simplicit&eacute; et rapidit&eacute; de mise en &oelig;uvre. Par contre en g&eacute;n&eacute;ral je trouve que ces syst&egrave;mes ne sont pas sp&eacute;cialement bon march&eacute;. Ici encore en naviguant sur le Net vous trouverez une multitude d&rsquo;informations. </p>
