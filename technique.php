<a name="parpaing"></a><h1>5.<b><u>Construire sa Piscine Traditionnelle avec M&eacute;thode Parpaings</u></b></h1>
<p>C&rsquo;est LA m&eacute;thode traditionnelle de construction d&rsquo;une <em><strong>piscine</strong></em>. On commence par faire une fondation p&eacute;riph&eacute;rique puis on monte les murs en parpaings en faisant des poteaux verticaux tous les 2m, une ceinture p&eacute;riph&eacute;rique haute et une ceinture p&eacute;riph&eacute;rique basse. Cette m&eacute;thode est parfaite pour un rev&ecirc;tement de la piscine de type liner ou PVC arm&eacute;.</p>
<p>Ce petit sch&eacute;ma montre parfaitement ce proc&eacute;d&eacute;&nbsp;: <a href="http://www.construire-sa-piscine.com/photos/parpaing.jpg">Sch&eacute;ma</a></p>
