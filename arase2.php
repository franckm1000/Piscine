<p align="left" class="Style49">Ces d&eacute;fauts vont &ecirc;tre corrig&eacute;s par l&rsquo;arase. En effet, comme dit dans la d&eacute;finition on va faire avec l&rsquo;<a href="arase.php" title="arase de la piscine">arase</a> le dernier niveau du mur. Ce dernier niveau sera d&rsquo;environ 5cm, et sera cette fois ci parfaitement &agrave; niveau sur tout le tour de la piscine.</p>
