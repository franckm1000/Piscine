<p align="left" class="Style49">L&rsquo;arase est faite de planches de coffrage tout le tour. On part des <a href="filtration1.php" title="filtration de la piscine">skimmers</a> (de cette fa&ccedil;on on est sur qu&rsquo;ils sont proches du dessus de la piscine) et on met les planches au fur et &agrave; mesure en v&eacute;rifiant le niveau &agrave; chaque fois. Prenez bien votre temps pour y faire et v&eacute;rifiez plusieurs fois votre niveau, car les d&eacute;fauts se verraient tout de suite. Honn&ecirc;tement, si il y a un poste dont je suis assez fi&egrave;re c&rsquo;est bien l&rsquo;arase car elle est vraiment parfaite&nbsp;!</p>
