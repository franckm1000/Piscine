<p align="left" class="Style49">Si je fais un chapitre &agrave; part sur l'arase, c'est pour une raison toute particuli&egrave;re. L'arase va en effet d&eacute;terminer &agrave; &quot;quel point&quot; la <u><b>piscine</b></u> sera &quot;&agrave; niveau&quot;. Cela est tr&egrave;s important, car une fois remplie d'eau toute diff&eacute;rence de niveau sur la p&eacute;riph&eacute;rie du bassin se verrait imanquablement (rien de plus vrai que l'eau pour voir un niveau). Il me semble donc n&eacute;c&eacute;ssaire de bien faire attention &agrave; cette partie de la construction.</p>

<p align="left" class="Style49">Voici la d&eacute;finition que l&rsquo;on peut trouver pour le mot <b><u>Arase</u></b>&nbsp;:</p>
<p align="left" class="Style49">&laquo;&nbsp; Dernier niveau d'un mur ou d'une construction et servant de rep&egrave;re pour construire dessus&nbsp;&raquo;</p>
<p align="left" class="Style49">On a mont&eacute; successivement six rang&eacute;es de blocs pour atteindre une hauteur de 1,2 m&egrave;tre, sachant que l&rsquo;on a mont&eacute; la premi&egrave;re rang&eacute;e au mortier pour la mettre &agrave; niveau. Or malgr&eacute; ces pr&eacute;cautions, on s&rsquo;aper&ccedil;oit que l&rsquo;ensemble de la piscine n&rsquo;a pas un niveau horizontal parfait. Pas de panique, l&rsquo;arase va nous servir &agrave; corriger ce d&eacute;faut&nbsp;!</p>
<p align="left" class="Style49">Par exemple, dans mon cas &agrave; deux endroits il y a un d&eacute;calage de niveau&nbsp;:</p>
