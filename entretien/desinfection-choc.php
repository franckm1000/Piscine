<div>
	<h2>Le traitement choc : Quand et pourquoi ?</h2>

	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	<p>Premi&egrave;rement, il faudra effectuer un traitement choc comme on l'a vu juste apr&egrave;s la mise en eau. Mais de temps en temps (le moins souvent possible!) vous serez amen&eacute;s &agrave; faire une traitement choc, principalement lorsque votre eau aura &quot;tourn&eacute;e&quot;.</p>

	<p>Si vous vous occupez de votre piscine correctement,vous n'aurez que tr&egrave;s peu &agrave; faire ce traitement (sauf en d&eacute;but et fin de saison).</p>

	<p> Par contre, imaginons que vous partez en vacances deux semaines : vous bachez votre piscine et arr&ecirc;tez votre filtration. Quand vous rentrez, en d&eacute;bachant, vous risquez d'avoir une surprise : eau et/ou parois de couleur verte etc...</p>

	<p>Dans ce cas, pas de panique, pas besoin de vider la piscine et de passer le karcher. Une bonne chloration choc fera l'affaire. Cette op&eacute;ration va permettre de purifier l&rsquo;eau rapidement et de mani&egrave;re efficace.</p>

	<p>Concr&egrave;tement, vous prenez votre traitement choc (chlore choc ou brome choc ou hypochlorite de calcium suivant votre traitement quotidien) et vous le dispersez dans la piscine. Si il se pr&eacute;sente sous forme de granul&eacute;s, vous commencez par y dissoudre dans un seau d'eau puis vous y dispersez dans la piscine. Regardez bien les indications de dosage sur la boite pour savoir combien en mettre.</p>

	<p>Une fois dans l'eau, attention tout ne va pas se faire tout seul ! Il faut mettre en route la filtration et commencer  &agrave; nettoyer la piscine avec un balai. Eh oui ! il va falloir frotter &eacute;nergiquement tous les recoins de la piscine pour &ecirc;tre sur que les algues ne vont pas rester quelque part.</p>

	<p>Au bout d'un petit moment, vous allez voir votre piscine retrouver un ton bleu. A ce moment, l'eau sera tr&egrave;s trouble car les algues seront en suspension dans l'eau. Il faut laisser tourner la filtration pendant 24 heures d'affil&eacute;e et normalement votre eau de piscine sera redevenue cristaline !</p>
