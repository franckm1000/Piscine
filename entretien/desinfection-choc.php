<div>
	<h2>Après la mise en eau : un traitement "choc"</h2>

	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>
	
	<p>La piscine est remplie, la mise en route s'est effectuée correctement maintenant il faut songer à la "désinfection" de l'eau de la piscine.
	 
	Celle ci se fait de façon continue pendant toute la saison : chlore, électrolyse, axygène actif... les solutions sont multiples mais nous verrons cela un peu plus en aval.
	 
	Par contre, tout de suite après la mise en eau, il faut commencer par faire ce qu'on appelle un "traitement choc". Cela va consister en un "surdosage" des éléments nettoyants.
	 
	Le plus courant consiste à faire ce qu'on appelle un "chlore choc". C'est un produit généralement en granulés ou liquide que l'on ajoute dans la piscine avec un dosage au m3.
	 
	Ces produits sont très impressionnants d'efficacité et serviront à chaque fois que l'eau aura "tournée".</p>
	 
