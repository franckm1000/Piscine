<div>
	 <h2>Le traitement de l'eau par le brome</h2>

	 <p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	<p>Le syst&egrave;me de traitement de l'eau naturel BIO-UV pour piscine et spa est tr&egrave;s simple.</p>

	<p> Un appareil de d&eacute;sinfection UV est install&eacute; sur le circuit de filtration. Une pompe doseuse, &eacute;galement plac&eacute;e sur le circuit de traitement d'eau du bassin UV, vient parfaire la purification de l'eau. Enfin et en option,&nbsp; une r&eacute;gulation automatique du pH compl&egrave;te le traitement UV de la piscine.</p>

	<p><br />

	Gr&acirc;ce &agrave; une lampe UV piscine ou &agrave; une lampe UV de traitement d'eau au sens large, la qualit&eacute; d'eau est irr&eacute;prochable. Le traitement d'eau de piscine ou de spa BIO-UV &eacute;vite l'usage d'un produit de piscine agressif et corrosif. Il s'agit d'un traitement de piscine sans chlore. </p>

 


	<p style="margin-top:74px;">Le brome est largement pr&eacute;sent dans la nature, et plus particuli&egrave;rement dans l'eau de mer.</p>
	<img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;padding-right: 11px;margin-top: -8px;" /></p>

	<p>Employ&eacute; dans le traitement de l'eau de piscine, le brome agit en tant   qu'oxydant, d&eacute;sinfectant et algicide au m&ecirc;me titre que le chlore,   puisque ce sont tous les deux des halog&egrave;nes.</p>

	<p>D&eacute;couvrez les avantages d'un traitement d'eau de piscine au brome.</p>

	<ul>

	<li style="margin-left:30px;"> Non-irritant<br />

	Au contact des déchets organiques, le brome produit des bromamines qui, contrairement aux chloramines dégagées par le chlore, présentent l'avantage de ne pas être irritantes pour les utilisateurs de la piscine.</li>

	<li style="margin-left:30px;">Produit complet<br />

	Le brome, à l'instar du chlore, a une action de désinfectant, d'oxydant et d'algicide sur l'eau de la piscine. Vous n'avez donc pas besoin de rajouter d'autre produits dans l'eau de votre piscine.</li>

	<li style="margin-left:30px;">Efficacité étendue à pH élevé<br />

	Le brome garde une efficacité optimum même en présence d'un pH elevé : à pH 8, il est encore actif à 80%, alors que le chlore ne l'est plus qu'à 20% dans des conditions similaires.</li>

	<li style="margin-left:30px;">Pratique<br />


	Son conditionnement en pastille ou en briquettes rend son usage très facile.<br />

	Sa dissolution lente pour le conditionnement en pastilles le rend très intéressant pour un traitement d'eau de piscine de base avec un minimum d'entretien.<br />

	Présenté sous forme de briquettes à 40%, sa dissolution est plus rapide.</li>

	<li style="margin-left:30px;">Sans stabilisant<br />

	Le brome ne contient pas de stabilisant, ce qui est particulièrement important lorsque les produits utilisés en parallèle en contiennent. En effet, il est donc plus facile pour vous de ne pas surcharger l'eau de votre piscine en stabilisant dont vous auriez beaucoup de mal à vous débarrasser.</li>


	<li style="margin-left:30px;">Note : Le seul point négatif à mettre sur le compte de brome est son prix ! Le brome est effectivement au bas mot 40% plus cher que le chlore. La pose d'un brominateur (indispensable pour le brome en pastilles) est également assez onéreuse.</li></ul>

	


