	<h2>L'oxyg&egrave;ne actif</h2>

 <p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	<p>L’oxygène actif permet de lutter contre les matières organiques nuisant à la clarté de l’eau. 

C’est un puissant oxydant des matières organiques, c'est-à-dire qu’il supprime toutes les nourritures nécessaires au développement des algues et débarrasse la piscine de toutes les impuretés organiques (bactéries, virus, champignons).

L'oxygène actif (H2O2) ou peroxyde d'hydrogène est un oxydant très puissant, complètement soluble dans l'eau, qui se décompose en eau et en oxygène.
 
L'oxygène actif, un produit "écologique". La réaction de l’oxygène actif et de l’eau de la piscine permet d’éliminer les matières organiques en ne formant aucun sous-produit, contrairement au chlore ou au brome. Elle ne laisse pour tout résidu que de l’eau pure. Le processus a même pour résultat l'augmentation de la concentration en oxygène dans l'eau.

Aujourd’hui, où l’on attache tant d’importance aux problèmes de pollution, c’est le produit idéal pour la désinfection de l'eau de votre piscine.

Par ailleurs, ce traitement permet d’obtenir une eau douce, sans odeur, sans irritation des yeux ni des muqueuses.</p>

	
