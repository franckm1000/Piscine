<div>
	<h2>Deux principaux types : Hypochlorite de Calcium et Hypochlorite de Sodium</h2>



	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	 

	<p>L'hypochlorite de sodium n'est ni plus ni moins que le nom barbare pour désigner de l'eau de javel si communément utilisée dans la vie courante pour désinfecter la maison. Son pouvoir désinfectant n'est plus à démontrer, et naturellement il s'adapte donc parfaitement à la désinfection d'une piscine.</p>

	<p>C'est un produit qui perd de son efficacité avec le temps, la lumière et  la température. La concentration des produits vendus atteint 50°B ce  qui équivaut à +/- 150 gr de chlore actif par litre. Lorsque ce  désinfectant est utilisé, il se forme de l’acide hypochloreux mais  également de la soude qui élève le pH de l’eau. Dans les installations  plus élaborées, l’hypochlorite est injecté par pompe doseuse asservie à  un régulateur (avec électrode : redox ou ampérométrique) qui mesure le  chlore libre.</p>

	<p>L’hypochlorite de calcium est très utilisé en piscine familiale sous  forme de galets que l’on dispose dans un chlorinateur ou dans les  skimmers. Le chlorinateur est un petit récipient fermé, inséré sur le  circuit de filtration. Une petite vanne permet le passage de l’eau qui  pénètre dans le récipient pour se charger en hypochlorite et retourner  au bassin.<br />

	Les galets sont vendus sous deux formes : à dissolution lente (à placer  dans le chlorinateur) ou rapide. Cette dernière est utilisée de manière  ponctuelle en cas de besoin : eau neuve, orage, fréquentation trop  importante des baigneurs…La concentration de ces produits varie de 65 à  80 % de chlore libre.<br />

	</p>

	 
	<p>L’action du chlore se réalise de la manière suivante :<br />

	<br />

	Mélangé à l’eau, un équilibre entre acide hypochloreux et hypochlorite  s’établit et les proportions des deux dépendent du pH. Ces deux formes  chlorées donnent le chlore libre.<br />

	Aussi, un pH bas favorise la formation d’acide hypochloreux qui est la  forme active (désinfectante) du chlore. Pour déterminer le chlore  actif, (le chlore qui agit, qui désinfecte) il faut donc tenir compte  du pH. En pratique, on utilise donc un tableau indiquant la quantité de  chlore actif contenue dans le chlore libre en fonction du pH. <br />

	<br />

	A ce stade, il faut savoir que la concentration en chlore actif dans le  bassin doit se situer à des valeurs comprises entre 0,4 et 1,4 mg  /litre.</p>
