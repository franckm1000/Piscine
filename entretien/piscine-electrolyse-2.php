<h1>L'entretien de la Piscine - G&eacute;n&eacute;ralit&eacute;s</h1>
     
<div>

	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>


	<p>L'exlectrolyse au sel  est <strong>&eacute;conomique</strong> et <strong>&eacute;cologique.</strong> <br />

	Plus d'irritations des yeux, d'allergies, de  dess&egrave;chement de la peau, d'odeurs. Plus de corv&eacute;es de  manipulations de produits chimiques, entretien facile. </p>

	<p>

	Une fois install&eacute; dans le circuit de filtration, votre  &eacute;lectrolyseur produit de l&rsquo; <strong>HYPOCHLORITE DE SODIUM</strong> (chlore actif naturel) par &eacute;lectrolyse du sel que vous aurez  dilu&eacute; dans l'eau de votre piscine (3 grammes de sel par litre  d&rsquo;eau soit environ 10 fois moins que dans l'eau de mer). </p>

	<p>

	Concr&egrave;tement, on fait circuler l&rsquo;eau de la piscine dans une <strong>CELLULE DE PRODUCTION</strong> constitu&eacute;e d&rsquo;un jeu d&rsquo;&eacute;lectrodes en <strong>TITANE</strong> recouvertes de <strong>RUTHENIUM et d&rsquo; IRIDIUM</strong> un courant &eacute;lectrique basse tension de 2 &agrave; 12 amp&egrave;res alimente ces &eacute;lectrodes.  Ce courant va dissocier la mol&eacute;cule de sel <strong>NaCl</strong> en ions chlore et sodium. Ainsi, le chlore, sous forme d&rsquo;hypochlorite  de sodium ou <strong>NaOCL</strong>, se combine &agrave; l&rsquo;eau pour former l&rsquo;acide  hypochloreux, un puissant oxydant-d&eacute;sinfectant.  L&rsquo;&eacute;lectrolyseur est install&eacute; en <strong>ASSERVISSEMENT</strong> avec la pompe de circulation de l&rsquo;eau. Un capteur de d&eacute;bit  est, soit incorpor&eacute; dans la cellule de production, soit en  s&eacute;rie dans la branche ou celle-ci est connect&eacute;e, afin  d&rsquo;assurer la production d&rsquo;hypochlorite <em><strong>uniquement </strong></em>quand  l&rsquo;eau circule entre les &eacute;lectrodes.Ce chlore actif naturel, apr&egrave;s  avoir d&eacute;truit les bact&eacute;ries et micro-organismes de  votre eau, se recomposera naturellement en sel sous l'action des UV  du soleil. Il n&rsquo;y a donc pas de consommation de sel par ce proc&eacute;d&eacute; d&rsquo;&eacute;lectrolyse.  Il faut n&eacute;anmoins effectuer p&eacute;riodiquement un apport de sel pour compenser la  perte d&rsquo;eau sal&eacute;e due au nettoyage du filtre ainsi que l&rsquo;apport d&rsquo;eau de pluie.  Ne pas tenir compte de l&rsquo;&eacute;vaporation car le SEL NE S&rsquo;EVAPORE PAS&nbsp;! </p>

	<p>

	Le <strong>Ph</strong> de l'eau doit &ecirc;tre maintenu entre 7.0 et 7.4 pour permettre une efficacit&eacute; maximale de l&rsquo;&eacute;lectrolyse.  Le <strong>Ph</strong> de l'eau est li&eacute; &agrave; sa duret&eacute;. Plus une eau est calcaire plus son Ph est haut (7.8 et plus) .  Il faudra r&eacute;guli&egrave;rement baisser le <strong>Ph</strong> d'une eau calcaire avec du <strong>&laquo;&nbsp;PH moins&nbsp;&raquo; </strong> (il est pr&eacute;f&eacute;rable d&rsquo;utiliser de l&rsquo;acide sulfurique que du chlorhydrique car celui-ci est plus agressif envers  les mati&egrave;res plastiques). La production conjointe du <strong>SODIUM</strong> avec le <strong>CHLORE</strong> &agrave; tendance &agrave; augmenter l&eacute;g&egrave;rement la valeur du <strong>Ph</strong>.  L&rsquo;utilisation d&rsquo;un syst&egrave;me de <strong>REGULATION AUTOMATIQUE DU PH</strong> est donc vivement recommand&eacute;e avec un &eacute;lectrolyseur au sel. </p>

	<p>

	Il est important, pour ne pas user pr&eacute;matur&eacute;ment le rev&ecirc;tement RUTHENIUM et IRIDIUM des  plaques en titane de la cellule de production, de ne pas faire  fonctionner l&rsquo;&eacute;lectrolyseur lorsque la temp&eacute;rature de l&rsquo;eau est inf&eacute;rieure &agrave; 15 &deg; C., &eacute;galement  de ne pas utiliser de <strong>SULFATE DE CUIVRE</strong> comme anti-algues, celui-ci d&eacute;truirait irr&eacute;m&eacute;diablement  les &eacute;lectrodes en <strong>TITANE</strong> de la cellule de production. </p>


