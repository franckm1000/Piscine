<h1>La mise en eau : Le cas de l'électrolyse</h1>
     
<div>
	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	<p>Nous allons voir dans le cas pratique la mise en eau pour un cas particulier : celui de l'&eacute;lectrolyse. Je ne recommenderai jamais assez cette technique de purification d'eau de piscine. Tr&egrave;s efficace, demande peu d'entretien et pas plus co&ucirc;teuse qu'un entretien classique au chlore.</p>

	<p>Un peu plus loin dans le menu, je d&eacute;taillerai exactement le fonctionnement de l'&eacute;lectrolyse mais l&agrave; nous allons surtout nous attarder &agrave; sa mise en route.</p>

	<p>Une fois la piscine remplie, on a vu que l'on devait faire un traitement choc. Dans le cas de l'&eacute;lectrolyse aussi mais l&agrave; une petite diff&eacute;rence, on fera un traitement non pas avec un chlore choc traditionnel mais avec ce qu'on appelle de <strong>l'hypochlorite de calcium</strong>. Cela est rendu n&eacute;c&eacute;ssaire par le fait que celui ci ne contient pas de stabilisant (terme que l'on expliquera un peu plus tard).</p>

	<p>Apr&egrave;s ce traitement, on laisse la filtration en route 24 heures (&agrave; ce moment, bien s&ucirc;r l'&eacute;lectrolyse n'est pas en marche).</p>

	<p>Au bout de 24 heures, arr&ecirc;ter la filtration. A ce moment, il va falloir rendre l'eau &quot;sal&eacute;e&quot;. Il faut donc acheter du sel sp&eacute;cial pour piscine (vous en trouverez chez votre pisciniste pr&eacute;f&eacute;r&eacute; sans probl&egrave;me). Perso, j'ai un jour trouv&eacute; chez les magasins Leclerc des sacs de 25kg de sel pour piscine &agrave; 6.5&euro;. En +, ayant du mal &agrave; les vendre, ils les faisaient &agrave; moiti&eacute; prix, du coup j'ai carr&eacute;ment achet&eacute; la palette et de ce fait j'ai du sel pour plusieurs ann&eacute;es !</p>

	<p>Il faut regarder sur votre manuel d'&eacute;lectrolyse le taux de sel n&eacute;c&eacute;ssaire (celui ci est exprim&eacute; en g/l). Par exemple, pour moi il fallait entre 4 et 5g de sel par litre. La piscine faisant 80m3, il fallait 80000*4.5=360000g de sel soit &quot;environ&quot; 360 kg. J'ai mis donc mis au d&eacute;part environ 15 sacs de 25kg de sel.</p>

	<p>Vous versez donc vos sacs de sel un peu partout dans la piscine en diss&eacute;minant le sel dans tous les endroits de la piscine. Laissez au moins 12 heures le temps que le sel fonde dans l'eau. Mettez en route votre filtration SANS L'ELECTROLYSE pendant 2 ou 3 heures le temps que le sel soit bien r&eacute;parti dans la piscine. Puis mettez en route votre &eacute;lectrolyse et laissez faire !</p>

	<p>Sur votre tableau de l'&eacute;lectrolyse, vous devriez certainement avoir une indication du taux de sel pr&eacute;sent dans l'eau. Cela vous permet de savoir si il vous faut rajouter du sel (en effet, si il y a un gros apport d'eau douce, pluie par exemple, il se peut que vous ayez besoin de rajouter un peu de sel).</p>

