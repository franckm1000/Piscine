		  <h2>L'électrolyse au sel : et après ?</h2>
	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	<p>Et bien apr&egrave;s, plus rien ! Laissez votre &eacute;lectrolyse tourner avec votre filtration, elle s'occupera de la d&eacute;sinfection de l'eau pour vous. Si en plus vous optez pour un mod&egrave;le avec &quot;inversion de polarit&eacute;&quot; vous n'aurez m&ecirc;me pas &agrave; nettoyer la cellule. Seulement, v&eacute;rifiez de temps en temps votre taux de sel et le cas &eacute;ch&eacute;ant, rajoutez en.</p>

	<p>Il y a une autre chose &agrave; ne pas oublier &agrave; la mise en service de votre &eacute;lectrolyse. On va parler un peu plus loin des caract&eacute;ristiques de l'eau (Ph . . .) et bien sachez que l'&eacute;lectrolyse n'aime pas &quot;l'eau dure&quot; c'est &agrave; dire l'eau calcaire. En france, la plupart des r&eacute;gions ont une eau dure. Il faut donc &agrave; la mise en eau faire un test de l'eau pour calculer sa duret&eacute;, c'est ce qu'on appelle le TH de l'eau. Par exemple, mon appareil stipule une eau avec un TH maximum de 25. Si votre eau est trop dure, il faudra lui adjoindre un traitement pour le faire baisser.</p>

	<p>Autre chose importante. Ne faites JAMAIS tourner votre &eacute;lectrolyse avec une temp&eacute;rature d'eau inf&eacute;rieure &agrave; 15&deg;C, vous risqueriez d'endommager la cellule. C'est le seul inconv&eacute;nient de l'&eacute;lectrolyse : la relative fragilit&eacute; de la cellule qui est &agrave; changer suivant les mod&egrave;les tous les 5 &agrave; 10 ans.</p>
