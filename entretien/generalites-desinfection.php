<div>
	<h2>Pourquoi &quot;désinfecter&quot; une Piscine ?</h2>

	</div>

	<div class="fond-bloc">

	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	<p>Même débarassée de ses impuretés visibles (feuilles...), l'eau contien encore en elle des micro-organismes qui riquent de la faire &quot;tourner&quot;. Il faut donc utiliser des produits désinfectants pour que l'eau puisse garder son aspect cristallin.</p>

	<p>	Il existe une multitude de produits et de méthodes utilisées pour désinfecter l'eau. Au rayon des produits, il faut bien sûr citer le chlore. On peut l'utiliser sous deux formes principalement : hypochlorite de calcium d'un côté et hypochlorite de sodium (eau de javel) de l'autre côté. C'est le traitement certainement le plus utilisé car le moins cher actuellement.</p>

	<p>A côté de ça, on trouve d'autres types de traitements :</p>

	 

	<ul>

	<p> le brome</p>

	<p>Conditionné en pastilles, tablettes et granulés, possède, comme le  chlore, un pouvoir désinfectant et stérilisant, sans effets secondaires  (odeur, et irritations de la peau et des yeux). Il reste efficace même  dans une eau à l'acidité élevée (jusqu'à un pH de 8) ou  particulièrement chaude.</p>

	 

	<p>le PHMB</p>

	<p> (polymère d'hexaméthylène biguanide) est un puissant désinfectant sous  forme liquide, qui ne peut cependant être utilisé dans une eau  contenant du chlore, du brome ou des algicides à base de sulfate de  cuivre.</p>

	 

	<p>L'oxygène actif </p>

	<p>Commercialisé sous forme de comprimés de peroxyde, a des vertus à la fois bactéricides,        fongicides et algicides. Sans odeur, insensible au PH de l'eau, il constitue une bonne alternative aux produits chlorés. Son efficacité perdure pendant trois semaines environ mais disparaît dans une eau trop chaude. </p>

	<p>
	L'ozone

	<br />
	L'ozone oxyde et élimine les champignons, les virus et les bactéries.  Ce traitement nécessite l'installation d'un appareil spécifique,  l'ozoniseur. Il est branché directement sur le système de filtration  d'une piscine et injecte de l'ozone dans le circuit d'eau de la  piscine. L'ozoniseur transforme automatiquement l'oxygène en ozone qui  possède un fort pouvoir désinfectant.<br />

	<br />


	Les ultra-violets<br />

	Le traitement par rayons ultra-violets nécessite l'installation d'un  appareil spécifique sur le système de filtration de la piscine. Les  rayons ultra-violets, diffusés par des lumières spécifiques, éliminent  efficacement les bactéries, les algues, les germes et les virus en  agissant directement sur leur ADN.<br />
	<br />
	<br />
	Les électrodes de cuivre et d'argent<br />

	Les électrodes de cuivre et d'argent sont directement intégrées au  système de filtration. L'hydroxyde de cuivre est constituée d'ions  cuivre qui éliminent les champignons et les algues et d'ions argent qui  désinfectent et détruisent les impuretés de l'eau.</p>

	<p>Dans les deux parties suivantes, on va detailler l'utilisation du chlore dans les piscines. On va déjà s'intéresser aux propriétés du chlore et puis ensuite voir dans la pratique sous quelle forme on peut l'utiliser.<br />

	</p>
	
