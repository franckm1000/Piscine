	<h2>Hivernage actif ou hivernage passif ?</h2>

	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" />Il existe deux m&eacute;thodes pour hiverner sa piscine. L'hivernage actif ou passif.</p>

	<p>1. <b>L'hivernage actif</b> : on va laisser sa piscine en fonctionnement tout l'hiver, c'est &agrave; dire que la filtration va continuer &agrave; fonctionner (de fa&ccedil;on plus ou moins importante pendant tout l'hiver). L'avantage est qu'il n'y a pas besoin de faire baisser le niveau d'eau et pas besoin de vidanger la tuyauterie. L'inconv&eacute;nient est le risque de gel dans les tuyaux. Pour l'&eacute;viter, il faudra faire tourner la filtration r&eacute;guli&egrave;rement voir m&ecirc;me 24h/24h en cas de gel prolong&eacute;. Ce syst&egrave;me est donc plut&ocirc;t r&eacute;serv&eacute; pour les r&eacute;gions du &quot;sud&quot; de la France.</p>

	<p>2. <b>L'hivernage passif</b> : on va arr&ecirc;ter la filtration pendant tout l'hiver. Avantage : plus de filtration, la pompe ne va donc pas fonctionner pendant l'hiver et donc moins d'usure et de consommation d'&eacute;lectricit&eacute;. Inconv&eacute;nient : il faut faire descendre le niveau d'eau en dessous de tous les tuyaux (skimmers et refoulements) et boucher la bonde de fond. En outre, il faudra vidanger la tuyauterie avant l'hiver pour &eacute;viter tout risque de gel &agrave; l'int&eacute;rieur de la tuyauterie.</p>

	<p>Dans la section suivante, on verra les pr&eacute;cautions en pratique &agrave; prendre pour bien hiverner sa piscine.</p>

	 

