	<h2>Remplissage du Filtre de la piscine</h2>

	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	
	<p>Le filtre est un des &eacute;l&eacute;ments essentiels de la filtration. Sh&eacute;matiquement, c'est une grosse bombonne remplie de <a href="http://www.granulatdemarbre.fr/14-sable-filtration-piscine" title="sable filtration piscine" target="_blank">sable filtration piscine</a>. L'eau aspir&eacute;e par la pompe passe &agrave; l'int&eacute;rieur, est &eacute;pur&eacute;e de ses impuret&eacute;s grace au sable et est renvoy&eacute;e ainsi propre dans la piscine.</p>

	 
	<p>On consid&egrave;re &agrave; cet instant que tous les &eacute;l&eacute;ments de la piscine et de la filtration sont install&eacute;s et donc que votre piscine est pr&ecirc;te &agrave; fonctionner.</p>

	<p>Il faut donc commencer par remplir le filtre. Pour cela, il faut du sable fin. Le mieux pour vous le procurer est d'aller chez votre pisciniste.</p>

	<p>Sachez qu'en dessous d'une puissance de 15m3/h, vous n'aurez besoin que de sable fin granulom&eacute;trie 0.4-0.8mm. Par contre au del&agrave; de 15m3/h il faudra au fond du filtre commencer par mettre du sable gros 3-5mm puis ensuite du sable fin 0.4-0.8mm.</p>

	<p>Pour le remplir, ouvrez le couvercle au dessus du filtre, et versez simplement votre sable dedans. Obturez bien l'arriv&eacute;e depuis la pompe (le tuyau qui sort au milieu du filtre) pour &eacute;viter que du sable ne parte vers la pompe.</p>

	<p>Pour vous donner un ordre d'id&eacute;e, pour un filtre de 15m3/h, il faut commencer par mettre 50kg de &quot;gravier&quot; puis 100kg de sable fin. Au total, il faut que le filtre soit rempli au 2/3.</p>

	<p>Le mieux encore une fois est d'aller voir votre pisciniste et en lui sp&eacute;cifiant votre filtre et sa puissance, il vous dira quelle quantit&eacute; et type de sable il faut prendre.<br />

	<br />

