	<h2> Du bon usage de la vanne 6 voies</h2>


	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	<p>Vous l'avez surement remarqu&eacute;, &agrave; c&ocirc;t&eacute; de votre filtre (ou au dessus &eacute;ventuellement) se trouve une vanne comportant six positions diff&eacute;rentes. Cette vanne est tr&egrave;s importante car c'est elle qui d&eacute;termine le trajet de l'eau &agrave; la sortie du filtre. Etudions les six positions possibles :</p>

	<p>1. Ferm&eacute; (closed) : La vanne est ferm&eacute;e, l'eau est &quot;emprisonn&eacute;e&quot; dans le filtre. Attention de ne pas mettre en route votre filtration avec la vanne ferm&eacute;e. En effet, l'eau arriverait depuis la pompe dans le filtre mais celle ci ne pouvant plus ressortir, vous verriez alors jaillir de l'eau par dessus le couvercle du filtre sous l'effet de la pression.</p>

	<p>2. Lavage (Backwash) : Cette position sert au lavage du filtre. Au fur et &agrave; mesure que vous allez filtrer, le sable pr&eacute;sent dans le filtre va retenir les impuret&eacute;s et vous vous en doutez au bout d'un moment le sable va se &quot;salir&quot; et pr&eacute;senter beaucoup d'impuret&eacute;s. Il faut donc de temps en temps proc&eacute;der &agrave; ce qu'on appelle un lavage du filtre de la piscine. En mettant sur cette position, l'eau va non pas arriver par le dessus du filtre pour descendre en bas vers les cr&eacute;pines mais emprunter le chemin inverse : l'eau va arriver par les cr&eacute;pines et ressortir par le dessus du filtre et &ecirc;tre envoy&eacute;e &agrave; l'&eacute;gout. De cette fa&ccedil;on toutes les impuret&eacute;s vont remonter &agrave; la surface du filtre et partir &agrave; l'&eacute;gout. Il faut faire cette manipulation environ 1 fois par semaine en pleine saison entre 30s et 1mn &agrave; chaque fois.</p>

	<p>3. Rin&ccedil;age (Rince) : Apr&egrave;s le Lavage, l'eau peut se trouver &ecirc;tre encore un peu sale. Dans ce cas, on proc&egrave;de &agrave; un rin&ccedil;age : le circuit reprend son cycle normal (l'eau arrive par le haut du filtre et est dirig&eacute;e vers les cr&eacute;pines en bas) mais au lieu que l'eau ne retourne dans la piscine elle va &agrave; l'&eacute;go&ucirc;t. Apr&egrave;s un lavage, il faut proc&eacute;der &agrave; un rin&ccedil;age syst&eacute;matique mais court (pas plus de 20 secondes).</p>

	<p>4. Recirculation : Dans cette position, l'eau va pass&eacute;e directement de la pompe dans les refoulements sans passer par le filtre. L'eau une fois envoy&eacute;e par la pompe au lieu de passer dans le filtre repard directement dans les refoulements de la piscine. Cette position est int&eacute;ressante lorsque l'on veut faire des tests. Imaginez par exemple une situation o&ugrave; vous avez une fuite, ou par exemple vos refoulements crachent un peu de sable ou encore l'eau renvoy&eacute;e dans la piscine n'est pas tr&egrave;s propre. En mettant en recirculation, vous pouvez voir si votre probl&egrave;me vient du filtre ou pas.</p>

	<p>5.Ego&ucirc;t (Waste) : Dans cette position, l'eau part directement &agrave; l'&eacute;gout. Cette position sert en fait principalement quand on d&eacute;sire vidanger la piscine.</p>

	<p>6 Filtration : Position classique. C'est la position utilis&eacute;e en filtration.</p>

	<p>PS: j'utilise le terme &quot;egout&quot; de fa&ccedil;on abusive. En effet, comme je l'ai d&eacute;j&agrave; expliqu&eacute; plus en amont, l'&eacute;vacuation est g&eacute;n&eacute;ralement interdite par les &eacute;gouts de ville. Le mieux est encore il me semble de faire avec un puits perdu.</p>

	 

	<h2>Amor&ccedil;age de la pompe de la piscine</h2>


	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	<p>Certainement une des op&eacute;rations les plus d&eacute;licates lors de la mise en route de la filtration. En effet, il va falloir &quot;amorcer&quot; la pompe autrement dit lorsque l'on va mettre la pompe en route celle ci va au d&eacute;but &quot;aspirer de l'air&quot; et il va falloir faire en sorte qu'elle se mette &agrave; aspirer l'eau.</p>

	<p>Commencez par remplir la pompe avec de l'eau. Pour cela, ouvrez le couvercle du panier et versez de l'eau jusqu'&agrave; ce que la pompe soit pleine et refermez bien ensuite le couvercle.</p>

	<p>Ensuite, fermez la vanne des skimmers et ouvrez celle de la bonde de fond. Mettez la vanne sur filtration et ouvrez les vannes qui vont aux refoulements. Mettez en marche la pompe, celle ci doit aspirer l'eau en prevenance de la bonde de fond. Au d&eacute;but elle aspirera l'air pr&eacute;sent dans les tuyaux mais si tout va bien au bout d'un moment elle aspirera l'eau en preovenance de la bonde de fond.</p>

	<p>Ensuite, laissez la vanne de la bonde de fond ouverte &agrave; 1/4 seulement et ouvrez la vanne des skimmers. Pour faciliter l'amor&ccedil;age prenez un tuyau d'eau reli&eacute; &agrave; l'eau de ville (ou du puits) et ins&eacute;rez le dans un des skimmers. De cette fa&ccedil;on en introduisant &quot;artificiellement&quot; de l'eau dans la tuyauterie des skimmers, on facilite l'amor&ccedil;age.</p>

	<p>Au bout d'un moment, la pompe ne doit plus aspirer que de l'eau et plus d'air. Si vous voyez toujours des bulles d'air sortir au bout d'un moment des refoulements c'est qu'il y a un probl&egrave;me quelque part.</p>

	<p>Cet amor&ccedil;age fait une fois, il n 'y aura plus besoin de la faire. En effet, m&ecirc;me une fois arr&ecirc;t&eacute; (la nuit par exemple) le syst&egrave;me ne contenant plus d'air le lendemain la pompe doit repartir sans probl&egrave;me. En effet, gardez bien toujours &agrave; l'esprit que le circuit de filtration est un circuit en boucle o&ugrave; il ne doit jamais y avoir d'air (dans les tuyaux, pompe...). Si de l'air est pr&eacute;sent dans le syst&egrave;me c'est qu'il y a un probl&egrave;me et peut &ecirc;tre notamment une fuite au niveau de l'aspiration ( voir ci dessous, chapitre &quot;fuite ou pas fuite ?&quot;)</p>

	
	<h2>Mise en marche du robot de nettoyage</h2>

	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	<p>Une fois la pompe amorc&eacute;e, on va mettre en marche le robot polaris. Branchez le tuyau du robot sur la prise balai et au bout du tuyau branchez le robot.</p>

	<p>Dans le local technique, mettez en route la pompe principale. Ensuite mettez en route le surpresseur en ouvrant bien les vannes de ce dernier.</p>

	<p>V&eacute;rifiez que le robot est bien entra&iuml;n&eacute; au fond de la piscine et aspire les salet&eacute;s s'y trouvant.</p>

	<p>Vous pouvez alors r&eacute;gler la minuterie pour le faire marcher &agrave; intervalles r&eacute;gulier chaque jour. Pas la peine de le faire marcher 5 heures par jour. Personnellement, je le faisais marcher 1 heure le matin, 1/2 heure le midi et 1/2 heure le soir.</p>

	 
	<p align="center"><img src="/images/tableau.jpg" width="426" height="320" alt="tableau electrique" /></p>

	<p align="center">Tableau &eacute;lectrique du surpresseur. En haut &agrave; gauche, on voit la minuterie pour r&eacute;gler la marche jouirnali&egrave;re du robot. Notez bien que ce tableau (et donc sa minuterie) est asservie au tableau &eacute;lectrique principal. Cela veut dire que si le tableau principal est &agrave; l'arr&ecirc;t, le surpresseur du robot l'est &eacute;galement. Il faut bien s'assurer de ce bon branchement : en effet, imaginez le d&eacute;sastre si votre surpresseur marche alors que la filtration est en arr&ecirc;t&eacute;e.</p>


	<p align="center"><img src="/images/disjoncteur.jpg" width="199" height="203" alt="disjoncteur piscine" /></p>

	<p align="center">Si votre surpresseur disjoncte &agrave; l'usage, v&eacute;rifiez que l'amp&eacute;rage est suffisant. Dans ce cas, tournez la petite molette jusqu'&agrave; trouver le bon r&eacute;glage. En effet, si l'amp&eacute;rage est insuffisant, il se peut que le disjoncteur soit trop sensible et disjoncte de fa&ccedil;on intempestive &agrave; l'utilisation du surpresseur.</p>

	<p align="center">En cas de <a href="http://www.reparation-panne-robot-piscine.fr/" title="panne robot piscine" target="_blank">panne robot piscine</a>, il vous faudra faire appel &agrave; un sp&eacute;cialiste en la mati&egrave;re pour vous le remettre en &eacute;tat de marche.</p>

