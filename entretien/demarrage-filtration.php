	<h2>V&eacute;rification de la bonne marche de l'ensemble de la filtration Piscine</h2>

	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	<p>Commencez par v&eacute;rifier le manom&egrave;tre du filtre. Il s'agit d'un appareil install&eacute; sur le dessus du filtre et qui sert &agrave; mesurer la pression de l'eau dans le filtre.</p>

	 

	<p align="center"><img src="/images/manometre.jpg" width="304" height="321" alt="manometre" /></p>

	 

	<p align="left">La pression est exprim&eacute;e en bar et doit &ecirc;tre inf&eacute;rieure &agrave; 1.5-1.6 bars. En g&eacute;n&eacute;ral, elle doit se situer autour de 1 bar. Si elle monte &agrave; 1.3 bar, c'est que le filtre est encrass&eacute; et il faut donc proc&eacute;der &agrave; un nettoyage du filtre.</p>

	<p align="left">Apr&egrave;s la mise en route de la filtration, v&eacute;rifiez donc bien que votre manom&egrave;tre indique une pression normale.</p>

	<p align="left">V&eacute;rifiez &eacute;galement que l'aspiration s'effectue correctement aux skimmers et la bonde de fond. V&eacute;rifiez &eacute;galement le bon fonctionnement de la prise balai avec le robot.</p>

	<p align="left">Enfin, v&eacute;rifiez que les refoulements refoulent correctement l'eau et qu'il n'y a pas de bulles d'air qui en sortent</p>

	 

	<h2>Fuite ou pas fuite ?</h2>

	<p><img src="images/icone-jaune.png" alt="icone jaune" width="42" height="40" style="float:left;" /></p>

	<p>Une fois en marche, une des premi&egrave;re questions que vous serez amen&eacute;s &agrave; vous poser est la suivante : ma piscine est elle parfaitement &eacute;tanche ou alors y a il une fuite quelque part ? Voyons &ccedil;a en d&eacute;tail.</p>

	<p>1. Comment d&eacute;celer une fuite : Pas de recette miracle, la seule fa&ccedil;on est de prendre le niveau de la piscine (avec un baton par exemple sur lequel on fait une marque) &agrave; un instant T puis de reprendre le niveau 24 heures plus tard.</p>

	<p>Si le niveau n'a pas boug&eacute;, alors vous pouvez estimer que votre piscine ne fuit pas. Si le niveau a baiss&eacute; de 2cm vous pouvez nourrir quelque angoisse quoique il faut tenir compte &agrave; certaines p&eacute;riodes de l'&eacute;vaporation naturelle de l'eau (surtout en &eacute;t&eacute;). Si vous &ecirc;tes en p&eacute;riode d'&eacute;vaporation, &agrave; c&ocirc;t&eacute; de la piscine, remplissez un r&eacute;cipient d'eau en mettant une marque et regardez le lendemain de combien il a baiss&eacute; &ccedil;a vous donne ainsi une id&eacute;e de l'&eacute;vaporation.</p>

	<p>2. O&ugrave; &ccedil;a se trouve une fuite en g&eacute;n&eacute;ral ? G&eacute;n&eacute;ralement, une fuite se trouve autour des pi&egrave;ces &agrave; sceller (skimmer, bonde de fond, refoulement, prise balai, spots) ou plus &quot;grave&quot; dans la tuyauterie. Dans le cas d'un liner, une fuite peut se trouver sur le liner si celui ci est d&eacute;chir&eacute; &agrave; une endroit.</p>

	 
	<p>3. Comment trouver o&ugrave; est la fuite ? Alors l&agrave; c'est la que les choses se compliquent s&eacute;rieusement. En effet, le plus dur (pour l'avoir v&eacute;cu) n'est pas de &quot;r&eacute;parer&quot; une fuite mais bien savoir d'o&ugrave; elle provient. Etudions les possibilit&eacute;s :</p>

	<p>- Skimmers : la fuite peut provenir des skimmers, fuite autour du skimmer ou dans le skimmer lui m&ecirc;me. Pour v&eacute;rifier, laisser le niveau d'eau baisser, si il s'arr&ecirc;te en dessous des skimmers c'est que la fuite se situe &agrave; ce niveau l&agrave;.</p>

	<p>-Refoulements, prise balai, spots, bonde de fond : la encore m&ecirc;me solution on laisse le niveau d'eau baisser jusqu'&agrave; ce qu'il stagne. Si le niveau s'arr&ecirc;te en dessous des spots par exemple, la fuite se situe certainement autour d'un spot.</p>

	<p>Tuyauterie en aspiration : cela veut dire que le fuite se situe entre les skimmers ou la bonde de fond et la pompe. La plupart du temps dans ce cas, il y aura des bulles d'air qui sortiront au niveau des refoulements. En effet, la pompe en aspirant aspirera &eacute;galement de l'air depuis la fuite et &agrave; ce moment cet air ressortira aux refoulements.</p>

	<p>Un eastuce pour voir si la fuite vient de la tuyauterie entre les skimmers et la pompe est celle ci : faites descendre le niveau d'eau en dessous des skimmers. Fermez la vanne d'arriv&eacute;e des skimmers dans le local technique. Versez de l'eau dans le bac des skimmers, si le niveau dans le bac descend, c'est qu'il y a une fuite dans la tuyauterie entre les skimmers et la pompe.</p>

	<p>Tuyauterie en refoulement : cela veut dire que la fuite se situe entre le local technique et les refoulements.</p>

	 
	<p>Voici la proc&eacute;dure g&eacute;n&eacute;rale de d&eacute;tection d'une fuite :</p>

	<p>- Si votre niveau d'eau descend plus rapidement filtration en marche, c'est que la fuite se situe dans la tuyauterie.</p>

	<p>- Si votre niveau descend plus rapidement filtration arr&ecirc;t&eacute;e, c'est que le fuite se situe dans le bassin.</p>


