<p>En effet, entre le devant de la piscine (côté escalier) et le côté opposé on a un
mètre de dénivelé, ce qui ne sera pas sans poser de problème au niveau du remblai.</p>
<img src="images/remblai2.jpg" alt="Remblai Piscine" style="width:600px;" />
<p>Dénivelé de la piscine.</p>
<img src="images/remblai3.jpg" alt="Remblai d'une Piscine" style="width:600px;" >
<p>1.La largeur vers les skimmers est l’endroit ou le remblaiement est le plus important à faire. Le problème est que la limite du terrain au fond n’est pas loin, environ 3 mètres (vers le grillage). Si on remblaie simplement, on va avoir une pente très importante et en plus la terre risque de glisser sur le terrain voisin en contre-bas.</p>
<p>2.Sur la longueur 2, le terrain vient en descendant jusqu’à la piscine, là pas de problèmes pour remblayer.</p>
<p>3.Sur la largeur du côté de l’escalier, pas de problème non plus, il faudra même décaisser un peu car la piscine a été enterrée plus bas que le niveau naturel du terrain.</p>
<p>4. Sur la longueur opposée, on voit la limite du terrain matérialisée par la ligne rouge. Au fond, il y aura certainement une pente après le remblai.</p>

