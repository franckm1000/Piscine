<p>Une fois la structure terminée, la tuyauterie tirée, on peut procéder au remblai autour de la piscine. Celui-ci sera réalisé par le terrassier. Le remblai est particulier car le terrain est en pente et cela va créer des problèmes.</p>
<p> Heureusement, mon terrassier connaît parfaitement son travail et il a trouvé la solution : l’enrochement !</p>


<h2>A. Le Tassement du Remblai</h2>
<p>Le problème principal avec le remblai est que la terre va se tasser et s’affaisser par endroits durant les deux premières années. De ce fait, il est déconseillé de faire sa terrasse tout de suite après le remblai.</p>
<p>Une solution à ce problème est de faire des poteaux de soutien tous les deux mètres autour de la <a href="http://piscine.comprendrechoisir.com/" title="piscine" target="_blank">piscine</a> en parpaing. De cette façon, la future terrasse pourra prendre appui sur ces poteaux sans risques d’affaissement.</p>
<img src="images/remblai1.jpg" alt="Remblai de la Piscine" style="width:600px;" />
<p>Les poteaux sont fait avec des parpaings traditionnels et scellés au mortier.</p>
