<h2>C. Le problème au fond du terrain</h2>
<p>Regardons le fond du terrain d’un peu plus près&nbsp;:</p>
<img src="images/remblai4.jpg" alt="Remblai de la Piscine" style="width:600px;">
<img src="images/remblai5.jpg" alt="Remblai de la Piscine" style="width:600px;margin-top:10px;">
<p>On voit la limite du terrain sur la première photo. On a vu que remblayer directement n’était pas possible. Une solution aurait consisté à faire un mur en limite de hauteur à niveau avec la piscine, de cette façon on aurait un remblai plat au fond du terrain&nbsp;:</p>
<img src="images/remblai6.jpg" alt="Remblai Piscine" style="width:600px;">
<p>Cette solution a été vite écartée car techniquement compliquée et présentant des risques au niveau de la solidité. En effet, le problème est que à la limite du terrain, il y a tout de suite derrière une pente assez abrupte. Faire un mur à cet endroit aurait été risqué si on tient compte en plus de la poussée des terres.</p>
<p>Pendant un moment, pour éviter le remblai j’ai pensé faire une terrasse sur pilotis. Cette solution était envisageable mais pour moi le problème était le délai de réalisation. En effet, la quantité de travail pour réaliser cette terrasse est très importante et j’ai déjà peu de temps pour faire le reste (sans parler du site internet&nbsp;!).</p>
<p>Finalement, c’est mon terrassier qui m’a indiqué la solution&nbsp;: <b><i><u>l’enrochement</u></i></b>. Cela consiste à importer des gros rochers provenant de carrières et à les empiler les uns sur les autres sur la hauteur et la longueur souhaitée. Derrière, on pourra remblayer sans problèmes et il n’y a aucun risque pour la terre de glisser sur le terrain en bas.</p>
<img src="images/remblai7.jpg" alt="Remblai de Piscine" style="width:600px;">
<p>On voit sur cette photo les pierres issues de carrière ont étés positionnées au fond du terrain pour retenir la terre.</p>
<img src="images/remblai8.jpg" alt="Image Hosted by ImageShack.us" style="width:600px;">
