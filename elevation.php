<p>Le recouvrement d&rsquo;un fer doit &ecirc;tre de au moins 40 fois son diam&egrave;tre. Par exemple, dans notre cas pour un fer de 8mm de diam&egrave;tre, on doit faire un recouvrement d&rsquo;au moins 32cm.</p>
<p>Au niveau des angles de la <em><strong>piscine</strong></em>, du fait des d&eacute;coupes vous allez avoir des &laquo;&nbsp;trous&nbsp;&raquo; dans les angles. Il faudra donc faire un coffrage avec des angles &agrave; ce niveau pour que le b&eacute;ton ne coule pas. Si &agrave; certains endroits vous avez des petits trous dans les blocs, mettez un peu papier de sac de ciment pour boucher et &eacute;viter que le b&eacute;ton ne s&rsquo;&eacute;coule au travers.</p>
