<p>Une fois la premi&egrave;re ligne pos&eacute;e on continue de la m&ecirc;me fa&ccedil;on. On retire un cordeau sur une longueur et on pose les blocs &agrave; sec (le mortier sur la premi&egrave;re rang&eacute;e uniquement&nbsp;!). On fait bien attention &agrave; <b><u>CROISER</u></b> les blocs &agrave; chaque fois.</p>

<p>Vous monterez le nombre ad&eacute;quat de blocs, dans mon cas six rang&eacute;es soit 1,2m. De cette fa&ccedil;on j&rsquo;aurai 1,20m de hauteur dans le petit bain de la piscine et jusqu&rsquo;&agrave; 2m au plus profond de la pointe de diamant. Si vous optez pour un fond plat il faudra monter huit rang&eacute;es pour avoir 1,6m de hauteur de mur ce qui vous fera &agrave; peu pr&egrave;s 1,5m de hauteur d&rsquo;eau dans tout le bassin.</p>
<img src="images/elevation13.jpg" alt="Construire Une Piscine" name="Construire Une Piscine" style="width:600px;" />
<p>On pr&eacute;pare les blocs avant de les poser.</p>
