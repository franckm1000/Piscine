<p>Une fois la premi&egrave;re longueur pos&eacute;e, vous posez un cordeau sur une des deux largeurs. Normalement, si les piquets ont &eacute;t&eacute;s bien pos&eacute;s les cordeaux doivent &ecirc;tre parfaitement perpendiculaires.</p>
<p>Au niveau des angles vous aurez des d&eacute;coupes &agrave; faire, pour cela munissez vous d&rsquo;une disqueuse en diamant avec un bon diam&egrave;tre, de cette fa&ccedil;on vous pourrez travailler correctement.</p>
