<p align="left">Une fois le radier de la <em><strong>piscine</strong></em> coul&eacute; et sec, on peut commencer &agrave; monter les murs en bloc &agrave; bancher. Il faut apporter un grand soin au montage des blocs pour &eacute;viter toute malfa&ccedil;on qui serait d&eacute;sastreuse par la suite (mur de la <em><strong>piscine</strong></em> pas droit ou pas d&rsquo;aplomb).</p>

<p>Imaginez vous en train de vous baigner dans votre <em><strong>piscine</strong></em> et ne voyant que votre mur pas droit tout le temps. Apportez le plus grand soin dans la construction de la piscine  &agrave; tous le niveaux, on ne la r&eacute;petera jamais assez. Car toute malfa&ccedil;on peut se r&eacute;v&eacute;ler d&eacute;finitive. Ce serait alors dommage de passer autant de temps dans cette construction et de se faire g&acirc;cher le plaisir par une b&ecirc;tise.</p>
<p>On commence par monter la premi&egrave;re ligne. On utilise les rep&egrave;res plant&eacute;s au moment du terrassement aux quatre coins de la piscine. Commencez par tirer un cordeau entre deux rep&egrave;res d&rsquo;une longueur (par exemple avec du fil de p&ecirc;che). Posez le <b><u>premier</u></b> bloc de la longueur puis le <b><u>dernier</u></b> de cette m&ecirc;me rang&eacute;e.</p>
<p>Ensuite vous poserez un par un les blocs &agrave; la suite. A chaque bloc il faut v&eacute;rifier le niveau&nbsp; horizontal et l&rsquo;aplomb vertical. Pour le niveau horizontal utilisez un niveau &agrave; bulle et pour l&rsquo;aplomb vertical un fil &agrave; plomb.</p>

<p>Si votre radier<em><strong> piscine</strong></em> est parfaitement &agrave; niveau vous pourrez poser votre premi&egrave;re rang&eacute;e &agrave; sec mais dans la plupart des cas (comme moi&nbsp;!) vous devrez reprendre les imperfections de niveau du <a href="ferraillage.php" title="radier piscine">radier</a>. Pour cela le mieux est de poser la premi&egrave;re rang&eacute;e au mortier.</p>
