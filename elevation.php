<p>Trois rang&eacute;es de mont&eacute;es, plus que trois&nbsp;!!!</p>
<p>A chaque fois que vous aurez mont&eacute; une rang&eacute;e de blocs, il faudra bien penser &agrave; poser le ferraillage horizontal. Ne rigolez pas, mais j&rsquo;ai oubli&eacute; de mettre les fers sur une rang&eacute;e, j&rsquo;ai du enlever tous les blocs de la ligne suivante apr&egrave;s m&rsquo;en &ecirc;tre aper&ccedil;u&nbsp;! Je vous conseille de mettre deux fers de huit horizontaux &agrave; chaque ligne. Vous les posez simplement sur les blocs, il y a des encoches pr&eacute;vues &agrave; cet effet.</p>
<p>Il faut &eacute;galement penser &agrave; croiser les fers &agrave; chaque angle de la piscine.</p>
