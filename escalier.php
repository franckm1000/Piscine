<p>Coupe de l&rsquo;escalier de la Pisicne</p>
<p>Une vue de l&rsquo;escalier de la Piscine avant sa r&eacute;alisation&nbsp;:</p>
<img src="images/escalier3.jpg" alt="Piscine" style="width:600px;" />
<p>Pour r&eacute;aliser l&rsquo;escalier, on commence par se fabriquer une planche de coffrage de longueur de 2m et de hauteur de 30cm.</p>

<p>On installe ensuite une rang&eacute;e de parpaings creux de 20x20x50 sur la longueur du par terre de cette fa&ccedil;on&nbsp;:</p>
<img src="images/escalier4.jpg" alt="Piscine"style="width:600px;" />
<p align=center class="Style49">(excusez pour le dessin&nbsp;!)</p>
<p>On met devant ces parpaings la planche de coffrage fabriqu&eacute;e. Ensuite, 40cm plus en profondeur, on installe deux rang&eacute;es de parpaings de 20 (qui repr&eacute;sentent la deuxi&egrave;me marche).</p>
<img src="images/escalier5.jpg" alt="Piscine" style="width:600px;" />
<p>Ensuite, on peut couler la marche en b&eacute;ton. On voit sur la photo pr&eacute;c&eacute;dente que l&rsquo;on peut mettre du remblai dans l&rsquo;escalier. On a &eacute;galement des fers de reprise depuis le sol et dans les murs pour bien solidifier l&rsquo;ensemble. </p>

<p>Le circuit en PVC rigide est l&agrave; pour le bain bouillonnant, d&eacute;taill&eacute; dans le chapitre suivant.
<div class="note">QUEL TYPE D'ESCALIER?
<p>Il faut distinguer deux types d'escalier pour Piscine. D'un côté, les escaliers extérieurs comme cellui-ci et de l'autre côté les escaliers intérieurs, c'est à dire que ces escaliers sont à l'intérieur de la Piscine.</p>
<p> Pour les escaliers intérieurs, on peut faire par exemple un escalier sur toute une largeur de la piscine avec les nombre de marches adéquates pour descendre. Ce type d'escalier est très beau visuellement. On peut opter pour un escalier d'angle, plus facile à mettre en place et très pratique (voir le blog de shumi).</p>
<p>Pour les escaliers extérieurs, on peut soit opter pour une escalier rectangulaire comme le mien. On peut aussi opter pour un escalier de type roman, ceux les plus en vogue actuellement.</p>
<p>On peut placer l'escalier où bon vous semble. L'emplacement le plus classique étant celui sur une des deux largeurs en face des skimmers de la Piscine.</p><!--ferme note -->          
