<p>Bon, quelques années se sont passées depuis la construction de la piscine et les dernières photos posées datant de 2009, je pense qu'il était temps
d'en ajouter quelques unes plus récentes.</p>
<p>En plus, il faut noter que la terrasse a beaucoup changée car le carrelage qui avait été posé à l'époque a été enlevé pour faire place à un
autre revêtement: le granulat de marbre.</p>
<p>Je dois avouer que le carrelage était une mauvaise idée. Peut-être la dalle a été mal faite, c'est vrai aussi mais du coup la terrasse avait bougé
et des carreaux ont commencé à se fissurer. Surtout les joints s'écartent et perdent leur couleur.</p>
<p>Bref, connaissant un client qui faisait ce revêtement assez mal connu, je l'ai fait venir pour qu'il regarde par lui même quoi faire
et il nous a refait la terrasse entièrement avec ce granulat de marbre.</p>
<p>Le résultat est vraiment à la hauteur. Ce revêtement est réellement magnifique, en outre il est poreux et du coup l'eau ne stagne jamais. Ainsi,
on y glisse beaucoup moins que sur un revêtement de type carrelage.</p>
<p>Donc voici quelques photos prises cette année.</p>
