<p>Ce qu&rsquo;on appelle le Radier de la <em><strong>piscine</strong></em> est en fait la dalle sur laquelle va reposer la <em><strong>piscine</strong></em>. Pour les dimensions de celle-ci il faut compter les dimensions int&eacute;rieures de la <em><strong>piscine</strong></em> soit 10x5. On va rajouter tout autour 50cm (pour poser les blocs &agrave; bancher et laisser un peu de marge pour la tuyauterie ou autre). Donc en tout on fera un radier de 11x6m.

<p>Etant donn&eacute; la grandeur de la <em><strong>piscine</strong></em> (10mx5m) et le rev&ecirc;tement pr&eacute;vu (Carrelage) il fallait construire un Radier &eacute;pais et bien ferraill&eacute;.</p>

<p>Nous avons donc opt&eacute; pour 20cm d'&eacute;paisseur ce qui est g&eacute;n&eacute;ralement pr&eacute;conis&eacute; pour ce type de<em><strong> piscine</strong></em>.</p>

<p>Le ferraillage sera compos&eacute; de deux nappes de treillis soud&eacute;s espac&eacute;es entre elles de 10cm.</p>





<p>Ce poste de la construction ne doit pas &ecirc;tre sous estim&eacute; car il est la base de la <em><strong>piscine</strong></em> et toute malfa&ccedil;on &agrave; ce niveau peut se r&eacute;v&eacute;ler d&eacute;sastreuse.

<p>Voici la proc&eacute;dure utilis&eacute;e :</p>

<p>1. Nous avons pos&eacute; un lit de gravier sorte de drain.</p>

<p>2. Nous posons un film polyane sur tout le bassin (de cette fa&ccedil;on en coulant le b&eacute;ton celui-ci ne s&rsquo;enfonce pas dans le lit de gravier, et en plus on est s&ucirc;r que le treillis ne repose pas sur la terre).</p>

<p>3. Pose de petits plots de 5cm d'&eacute;paisseur un peu partout dans le bassin.</p>

<p>4. Pose du premier treillis soud&eacute;. Gr&acirc;ce aux plots, celui ci ne repose pas sur le sol. Pour le treillis soud&eacute;, utilisez du treillis d&eacute;nomm&eacute; <b><u>ST25 </u></b>. <em><strong>Moi, j&rsquo;ai utilis&eacute; du PAFC mais j&rsquo;aurais su j&rsquo;aurais utilis&eacute; du ST25</strong>.</em> Maintenant j&rsquo;ai la chance d&rsquo;avoir un terrain tr&egrave;s meuble et pas du tout argileux, avec une nappe phr&eacute;atique tr&egrave;s profonde. De ce fait, les contraintes sur le radier de la <em><strong>piscine</strong></em> sont moins importantes. En outre, le bassin a &eacute;t&eacute; fait maintenant il y a plus de six mois et aucune micro fissure en vue&nbsp;!</p>

<p>5. Nous posons un cha&icirc;nage 10x10 tout autour du bassin l&agrave; ou serons mont&eacute;s les blocs. Cela ressemblera &agrave; une ceinture p&eacute;riph&eacute;rique.</p>



<p>6. Nous disposons &eacute;galement des morceaux de cha&icirc;nage 10x10 un peu partout dans le bassin, de cette fa&ccedil;on les deux treillis soud&eacute;s seront bien espac&eacute;s entre eux de 10cm.</p>

<p>7. Nous posons le deuxi&egrave;me treillis soud&eacute;. Attention, il faut bien attacher les treillis entre eux une fois pos&eacute; sinon ils vont bouger sans arr&ecirc;t. Ne pas sous estimer le temps pour y faire car c'est relativement long (merci Pascal pour ton aide!).</p>

