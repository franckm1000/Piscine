<p>Apr&egrave;s avoir ferraill&eacute; la structure de la piscine, il a fallu coffrer le tour de la piscine pour d&eacute;limiter le radier. J&rsquo;ai achet&eacute; des planches sp&eacute;cial coffrage de 25cm de hauteur de 3m de longueur. Pour les faire tenir, nous avons clou&eacute;s des piquets contre les planches puis enfonc&eacute;s ces piquets dans le sol pour les faire tenir droite.</p>

<p>Ensuite, nous avons trac&eacute; un niveau sur tout le p&eacute;rim&egrave;tre des planches. C'est-&agrave;-dire&nbsp;:</p>

<p> 1. On est parti du coin vers l&rsquo;escalier et on a prit une hauteur de 20cm depuis le sol et tracer un niveau.</p>

<p>2. Avec un cordex, on a report&eacute; ce niveau sur la totalit&eacute; du p&eacute;rim&egrave;tre de la piscine. De cette fa&ccedil;on, le radier est parfaitement &agrave; niveau &agrave; chaque endroit sur le tour de la piscine.</p>

<p>3. Aid&eacute; d&rsquo;un niveau laser de chantier, on a mis des piquets &agrave; plusieurs endroits dans la piscine repr&eacute;sentant la hauteur finie du radier.</p>

