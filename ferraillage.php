<h3>Paragraphe sur les &laquo;&nbsp;semelles de reprise</h3>

<p>La liaison entre le radier (dalle) et le futur mur en agglos est primordiale. En effet, en liant les deux ensemble par un ferraillage adapt&eacute;, la structure de la <a href="index.php" title="piscine">piscine</a> est v&eacute;ritablement unie en un seul bloc et peut de cette fa&ccedil;on r&eacute;sister aux contraintes de bien meilleure fa&ccedil;on.</p>

<p>Pour ce faire, on utilise des &laquo;&nbsp;semelles de reprise&nbsp;&raquo;, c'est-&agrave;-dire des fers qui ressortent du radier pour aller se loger dans le mur, plus pr&eacute;cis&eacute;ment dans chaque alv&eacute;ole des <a href="technique.php" title="bloc &agrave; bancher">blocs &agrave; bancher</a>.</p>

