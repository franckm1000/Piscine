<p>Pour la taille, le standard des<em><strong> piscines </strong></em>est de 8x4m en g&eacute;n&eacute;ral mais comme mon terrain est assez grand nous avons d&eacute;cid&eacute; de la construire en 10x5m. En outre, la faisant nous m&ecirc;me le surplus financier n&#8217;&eacute;tait pas &laquo;&nbsp;&eacute;norme&nbsp;&raquo;.</p>
<p>Pour le rev&ecirc;tement, assez rapidement j&#8217;ai choisi le carrelage, peut &ecirc;tre pas tr&egrave;s en vogue actuellement mais visuellement sans &eacute;quivalent je trouve. En tout cas, plusbeau qu&#8217;une<em><strong> piscine </strong></em>en liner ou en PVC &agrave; mes yeux. Je trouve qu&#8217;actuellement il y a un d&eacute;nigrement syst&eacute;matique des <em><strong>piscines</strong></em> &agrave; rev&ecirc;tement carrelage carbeaucoup de piccinistes ne proposent que des liners ou du PVC. Ils vont vous dire que le carrelage &ccedil;a ne se fait plus mais en fait c&#8217;est qu&#8217;ils ne savent pas y faire ou que c&#8217;est trop long et trop co&ucirc;teux &agrave; faire. Mais regardez les <em><strong>piscines</strong></em> publiques, les trois quarts sont en carrelage, &ccedil;a n&#8217;est pas pour rien&nbsp;!</p>
<p>Restait &agrave; savoir quel proc&eacute;d&eacute; utiliser pour la construire. J&#8217;ai laiss&eacute; assez rapidement les kits en acier, inox etc. car relativement cher quand on y regarde bien. Ensuite je me suis port&eacute; vers des blocs en polystyr&egrave;nes tr&egrave;s int&eacute;ressants car l&eacute;gers et facilement maniables. L&#8217;inconv&eacute;nient est qu&#8217;avec cette mati&egrave;re on est oblig&eacute; de poser un liner comme rev&ecirc;tement.</p>
<p>La m&eacute;thode traditionnelle, parpaings + poteaux raidisseurs (<a href="photos/parpaing.jpg" title="M&eacute;thode avec parpaing classiques">voir ce sch&eacute;ma</a>) aurait &eacute;t&eacute; une solution int&eacute;ressante mais encore une fois on est oblig&eacute; de pr&eacute;voir un liner ou du PVC arm&eacute; comme &eacute;tanch&eacute;it&eacute;, or j&#8217;&eacute;tais vraiment d&eacute;cid&eacute; &agrave; poser un rev&ecirc;tement carrelage.</p>
<p>Pour ma piscine il restait alors deux choix&nbsp;: soit une piscine en b&eacute;ton arm&eacute; monocoque ou une structure en bloc &agrave; bancher. La premi&egrave;re &eacute;tant beaucoup trop compliqu&eacute;e &agrave; mettre en &#339;uvre pour un particulier j&#8217;ai choisi naturellement la deuxi&egrave;me.</p>
<p>Il fallait aussi ensuite choisir un escalier car je ne voulais pas d&#8217;&eacute;chelle en inox. L&#8217;escalier int&eacute;rieur ne m&#8217;int&eacute;ressait pas il me restait le choix entre un escalier ext&eacute;rieur droit ou roman. J&#8217;aurais pr&eacute;f&eacute;r&eacute; l&#8217;escalier roman mais la pose des blocs &agrave; bancher en courbe &eacute;tait trop compliqu&eacute;e pour moi. J&#8217;ai donc d&eacute;cid&eacute; de faire un escalier droit d&#8217;une largeur de 2m et d&#8217;une profondeur de 1,&nbsp;2m. Il sera compos&eacute; de trois marches de 2m de long, 30cm de haut et 40cm de profondeur.</p>
<p>Ensuite, il a fallu d&eacute;terminer le type de fond de la piscine. Principalement, on distingue quatre types de fond &nbsp;:</p>
<p>1.Le fond plat (1,50m de profondeur en g&eacute;n&eacute;ral dans tout le bassin). Certainement la m&eacute;thode la plus utilis&eacute;e aujourd&#8217;hui. L&#8217;avantage c&#8217;est le c&ocirc;t&eacute; ludique de la piscine pour le jeu mais l&#8217;inconv&eacute;nient c&#8217;est l&#8217;impossibilit&eacute; de plonger.</p>
<img src="images/image003.gif" alt="Type profondeur piscine" name="Type profondeur piscine" style="width:322px;" />
<p>2. La piscine avec fond en pente compos&eacute;e&nbsp;: une partie plate &agrave; 1,2m en g&eacute;n&eacute;ral, une pente qui descend et une deuxi&egrave;me partie plate &agrave; 2m par exemple. Int&eacute;ressant mais difficile &agrave; mettre en &#339;uvre au niveau de la premi&egrave;re ligne de blocs &agrave; bancher (en effet beaucoup de d&eacute;coupes &agrave; faire dans le pente)</p>
<img src="images/image004.gif" alt="Type profondeur piscine" name="Type profondeur piscine" style="width:322px;" />
<p>3.La piscine en fond 4 pentes : int&eacute;ressant &eacute;galement car les pentes sont douces mais il y a deux inconv&eacute;nients&nbsp;: Un la difficult&eacute; de mise en &#339;uvre au niveau du radier car les pentes c&#8217;est toujours plus compliqu&eacute; et deux la piscine est enti&egrave;rement en pente il n&#8217;a a aucun endroit plat.</p>
<img src="images/image005.gif" alt="Type profondeur piscine" name="Type profondeur piscine" style="width:322px;" /></p>
<p>4.La piscine en pointe de diamant. Mon choix car cette m&eacute;thode pr&eacute;sente plusieurs avantages&nbsp;:</p>
<p>a. On a une partie en fond plat de 1,2m sur 4m de longueur pour le jeu etc.</p>
<p>b.On a une partie profonde a 2m pour le plongeon.</p>
<p>c.Au niveau de la mise en &#339;uvre, on a l&#8217;avantage d&#8217;avoir la m&ecirc;me hauteur de mur sur toute la p&eacute;riph&eacute;rie de la piscine soit 1,2m de hauteur de mur. Par contre, au niveau du radier piscine, la m&eacute;thode est plus difficile &agrave; mettre en &#339;uvre et couler le b&eacute;ton est plus difficile.</p>
