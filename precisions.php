<p>Quand on choisit de faire construire une piscine il faut commencer par déterminer la taille et la forme de celle-ci. Comme la construction serait faite par mes soins les formes compliquées étaient déjà proscrites car trop difficile de réaliser des courbes avec des blocs à bancher. Ce sera donc un rectangle !</p>


<p>On peut dans l'absolu construire une <em><strong>piscine</strong></em> en bloc &agrave; bancher avec des formes courbes, ce n'est pas impossible. Il y a pour cela deux solutions, la premi&egrave;re &eacute;tant d'acheter des blocs &quot;courbes&quot; comme les &quot;variblocs&quot; par exemple ( &agrave; voir <a href="http://www.bipbetons.fr/gris/pdf/fiches/varibloc.pdf" target="_blank">ici</a> ). Cette solution &agrave; le d&eacute;savantage d'&ecirc;tre relativement couteuse. La deuxi&egrave;me solution est de monter les blocs de fa&ccedil;on &agrave; courber le mur comme ceci : Construire une piscine.
<p>Comment faire des arrondis de murs? La seule diff&eacute;rence avec un mur droit c'est la longueur de votre agglo car vous prendrez soin de le couper au milieu. Vous vous retrouverez avec 2/3 d'agglos (le 3&egrave;me &eacute;tant cass&eacute; par le marteau) ce qui facilitera votre arrondi. La perfection sera obtenue avec l'enduit de finition. Si votre arrondi n'est pas tr&egrave;s prononc&eacute;, vous pouvez le construire avec l'agglo entier, vous aurez simplement plus d'enduit &agrave; talocher pour la finition.</p>
