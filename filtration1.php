<p><u>LE SAVIEZ VOUS ?</u></p>
<p>L'eau est un vecteur de bact&eacute;ries particuli&egrave;rement important. Quand il fait chaud plus particuli&egrave;rement et qu'il n'ya pas de circulation d'eau (eau &quot;croupie&quot;) les bact&eacute;ries et autres algues se d&eacute;veloppent tr&egrave;s vite.</p>
<p>A contrario, quand la temp&eacute;rature de l'eau descend en dessous d'un certain seuil (10&deg;C) les bact&eacute;ries et autres algues stoppent leur prolif&eacute;ration. C'est pourquoi il faut bien faire attention &agrave; ne pas hiverner sa piscine trop t&ocirc;t en saison et bien attendre que la temp&eacute;rature de l'eau descende en dessous de 10&deg;C.</p>
<p>C'est d'ailleurs pour cela que les lacs de montagne sont souvent si beaux et &quot;propres&quot;, la temp&eacute;rature de l'eau monte raremant ce qui fait que l'eau est &quot;naturellement&quot; cristaline.</p>
