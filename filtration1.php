<h3>a.Les Skimmers de la Piscine</h3>
<p>Comme on a dit plus haut, les skimmers sont des bouches d&rsquo;aspiration. Jusqu&rsquo;&agrave; 8x4m on en met un seul, au-del&agrave; il faut en mettre deux voir plus pour les tr&egrave;s grands bassins. Ils sont situ&eacute;s au niveau de la ligne d&rsquo;eau c&rsquo;est donc eux qui d&eacute;terminent la hauteur d&rsquo;eau, il faudra donc les placer le plus haut possible.</p>
<p>Ils sont constitu&eacute;s &agrave; l&rsquo;arri&egrave;re d&rsquo;un pr&eacute;-filtre qui permet de stopper les plus gros d&eacute;chets (feuilles...). Ils doivent &ecirc;tre install&eacute;s vents dominants en face, l&rsquo;id&eacute;al &eacute;tant sur largeur du bassin oppos&eacute;e &agrave; l&rsquo;escalier (mon cas).</p>
<div class="img_centre"><img src="images/skimmer.jpg" alt="Skimmer Piscine" style="width:200px;" /></div>
<p style="text-align:center">Skimmer</p>
<h3>b.Les Refoulements de la Piscine</h3>
<p>C&rsquo;est eux qui s&rsquo;occuperont de renvoyer l&rsquo;eau filtr&eacute;e dans la piscine. Ils sont donc &agrave; l&rsquo;oppos&eacute; des skimmers dans le circuit de nettoyage. On les placera donc &agrave; l&rsquo;oppos&eacute; des skimmers dans mon cas sur la largeur du c&ocirc;t&eacute; de l&rsquo;escalier.</p>
<p>Jusqu&rsquo;&agrave; 8x4m, on ne met deux, au-del&agrave; il en faut trois. Dans mon cas j&rsquo;en ai mis trois, deux de chaque c&ocirc;t&eacute; de l&rsquo;escalier et un troisi&egrave;me au milieu de l&rsquo;escalier.</p>
<div class="img_centre"><img src="images/refoulement.jpg" alt="Refoulement Piscine"  style="width:200px" /></div>
<p style="text-align:center">Refoulement</p>
<h3>c. La Bonde de Fond de la Piscine</h3>
<p>Elle est plac&eacute;e au point le plus profond de la piscine, dans mon cas au milieu de la pointe de diamant. Elle est scell&eacute;e dans le radier de la piscine. Elle sert &agrave; aspirer tous les &eacute;l&eacute;ments lourds plac&eacute;s au fond de la piscine et &agrave; aspirer l&rsquo;eau du fond. Elle participe donc activement &agrave; la filtration de la piscine.</p>
<p>Il y a un grand d&eacute;bat &agrave; ce sujet sur son utilit&eacute; car beaucoup de piscine maintenant n&rsquo;en sont plus pourvues. Il s&rsquo;agit des piscines avec bloc filtrant. Allez voir sur le forum <a href="http://www.eauplaisir.com/annuaire/forums/index.php">EauPlaisir</a>, c&rsquo;est un des d&eacute;bats pr&eacute;f&eacute;r&eacute;s du forum&nbsp;!</p>
