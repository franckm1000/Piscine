<p style="text-align:center">Bonde de Fond</p>
<h3>d. La Prise Balai de la Piscine</h3>
<p>Elle est scell&eacute;e elle aussi dans la piscine, de pr&eacute;f&eacute;rence sur la longueur. Elle est branch&eacute;e elle aussi en aspiration mais comme son nom l&rsquo;indique elle sert pour brancher un balai ou un robot (type Polaris 280).</p>
