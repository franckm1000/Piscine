<h2>II.La pompe de la Piscine</h2>
<p>&nbsp;</p>
<p>La plupart des pompes sont &eacute;quip&eacute;es d&rsquo;un pr&eacute;-filtre qui permet de r&eacute;cup&eacute;rer les plus gros d&eacute;chets. Elle sera install&eacute;e dans un local technique &agrave; proximit&eacute; de la piscine. Elle doit &ecirc;tre en position horizontale, le mieux &eacute;tant sur une chape en b&eacute;ton. Le calcul de la puissance de la pompe par rapport au bassin se fait de la fa&ccedil;on suivante.</p>
<p>On consid&egrave;re qu&rsquo;il faut que la filtration enti&egrave;re de la piscine soit faite en 6 heures. Si un bassin fait 60m3 par exemple, on dira qu&rsquo;il faut que la pompe fasse 60/6 = 10m3/h. Cela donne une id&eacute;e de la puissance de la pompe piscine &agrave; utiliser.</p>
<p>Il vaut mieux &agrave; partir de ce calcul surdimensionner un peu la pompe car il faut tenir compte du fait qu&rsquo;il y des pertes de puissance (dues &agrave; l&rsquo;&eacute;loignement de la pompe par rapport &agrave; la piscine, aux coudes&hellip;).</p>
<p>Personnellement, ma piscine contiendra &agrave; peu pr&egrave;s 75m3 d&rsquo;eau donc par calcul on en d&eacute;duit qu&rsquo;il faut une <b>pompe piscine</b> qui fasse environ 13m3. Mais pour des raisons de confort j&rsquo;ai pr&eacute;f&eacute;r&eacute; prendre une pompe plus puissante (21 m3/h).</p>
<p>L&rsquo;id&eacute;al est de mettre la pompe <em>en dessous</em>du niveau d&rsquo;eau de la piscine. Evitez au maximum de la mettre au dessus du niveau de l&rsquo;eau de celle-ci.</p>
<div class="img_centre"><img src="images/pompe.jpg" alt="Pompe Piscine" style="width:200px;" /></div>
<p>&nbsp;</p>
<h2>III.Le Filtre de la Piscine</h2>
<p>Le filtre est &laquo;&nbsp;l&rsquo;organe nettoyant&nbsp;&raquo; du syst&egrave;me de filtration, c&rsquo;est lui qui s&rsquo;occupe d&rsquo;&eacute;liminer les impuret&eacute;s de l&rsquo;eau de la piscine. On distingue trois types de filtre&nbsp;:</p>
<p>. Le filtre &agrave; sable utilis&eacute; dans les filtrations traditionnelles</p>
<p>. Le filtre &agrave; diatom&eacute;e beaucoup plus fin que le filtre &agrave; sable (mais beaucoup plus cher&nbsp;!)</p>
<p>. Le filtre &agrave; cartouche principalement utilis&eacute; dans les blocs de filtration</p>
<p align="center">&nbsp;</p>
<p align="center" class="style49">De gauche &agrave; Droite, les trois type de Filtres les plus usit&eacute;s : Sable, Diatom&eacute;es et Cartouche</p>
