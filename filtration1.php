<p>Pour nettoyer l&rsquo;eau d&rsquo;une piscine, il faut commencer par l&rsquo;aspirer, pour cela on utilise une <b>pompe piscine</b>. Pour aspirer l&rsquo;eau de la piscine on utilise des <b>skimmers</b>, sortes de bouches d&rsquo;aspirations rectangulaires situ&eacute;es d&rsquo;un c&ocirc;t&eacute; de la piscine au niveau de la ligne d&rsquo;eau. La pompe aspire l&rsquo;eau par les skimmers, la renvoie dans un <b>filtre piscine</b> qui s&rsquo;occupe de la d&eacute;barrasser des &eacute;l&eacute;ments polluants, puis cette eau &laquo;&nbsp;purifi&eacute;e&nbsp;&raquo; sera renvoy&eacute;e dans la piscine au moyen de <b><u>buses de refoulement</u></b>.</p>
<p>En outre en plus des skimmers il y a un autre &eacute;l&eacute;ment d&rsquo;aspiration, la <b>bonde de fond</b> situ&eacute;e au plus profond de la piscine et qui permet d&rsquo;aspirer l&rsquo;eau et les polluants situ&eacute;s au fond de la piscine.</p>
<p>Tous ces &eacute;l&eacute;ments combin&eacute;s conduisent &agrave; cr&eacute;er un mouvement permanent de l&rsquo;eau permettant d&rsquo;avoir une eau propre tout le temps.</p>
<p>C&rsquo;est ce principe de base qui est utilis&eacute; dans tout type de filtration. Il existe ensuite deux m&eacute;thodes pour la mettre en &oelig;uvre, les blocs de filtration et la filtration par m&eacute;thode classique.</p>
<p>Les blocs filtre int&egrave;grent tous les &eacute;l&eacute;ments de la <b>filtration piscine</b> en un seul &eacute;l&eacute;ment (pompe, filtre, &eacute;l&eacute;ments d&rsquo;aspiration et de refoulement de l&rsquo;eau. . .). Leur avantage est donc leur <b>compacit&eacute;</b>. Il suffit d&rsquo;adosser ce bloc &agrave; un endroit de la piscine, on a donc une installation des plus simple. En outre, ces derni&egrave;res ann&eacute;es, les progr&egrave;s techniques r&eacute;alis&eacute;s permettent d&rsquo;avoir de tr&egrave;s bons r&eacute;sultats avec ce type de bloc.</p>

<p>La m&eacute;thode classique est celle qui utilise un <a href="localtechnique.php" title="local technique piscine">local technique</a>. Dans ce cas, tous les &eacute;l&eacute;ments sont s&eacute;par&eacute;s&nbsp;: filtre, pompe, skimmers, refoulements. La pompe et le filtre sont situ&eacute;s dans un local technique a proximit&eacute; de la piscine, les &eacute;l&eacute;ments d&rsquo;aspiration et de refoulement dont scell&eacute;s dans la structure de la piscine et le tout est reli&eacute; par un syst&egrave;me de tuyauterie en PVC.</p>

<p>Voyons maintenant chaque &eacute;l&eacute;ment de plus pr&egrave;s.</p>
<h2>I. Les &eacute;l&eacute;ments &agrave; sceller de la Piscine</h2>
