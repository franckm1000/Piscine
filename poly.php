                <p>Par contre, le bloc polystyr&egrave;ne pour <a href="index.php" title="piscine">Piscine</a> a un avantage non n&eacute;gligeable&nbsp;: une fois la structure mont&eacute;e, vous pouvez de suite poser le liner contrairement au bloc en&nbsp; b&eacute;ton o&ugrave; avant la pose du liner vous devrez r&eacute;aliser un enduit lisseau mur en mortier ciment.</p>
                <p>On le voit par rapport au bloc b&eacute;ton, le bloc polystyr&egrave;ne peut &ecirc;tre int&eacute;ressant dans le cas d&rsquo;une &eacute;tanch&eacute;it&eacute; par liner ou PVC arm&eacute;. Mais je le r&eacute;p&egrave;te, si comme dans mon cas, vous d&eacute;sirez un carrelage, alors vous n&rsquo;aurez pas le choix ce sera bloc en b&eacute;ton&nbsp;!</p>
                
                <h2>C.Le co&ucirc;t de revient de la Piscine en Bloc Poly</h2>
                <p>Il faut voir maintenant au niveau du co&ucirc;t. Il existe beaucoup de &laquo;&nbsp;kit
                  polystyr&egrave;nes&nbsp;&raquo;&nbsp; vendus dans le commerce. Personnellement, je vous
                  conseille plut&ocirc;t d&rsquo;acheter les blocs polystyr&egrave;nes &agrave; part puis d&rsquo;acheter le reste (filtration, &eacute;l&eacute;ments &agrave; sceller&hellip;).</p>
                <p>Vous trouverez des kits polystyr&egrave;nes &agrave; moins de 4000&euro; pour une 10x5m mais en achetant les blocs puis la filtration et les &eacute;l&eacute;ments &agrave; sceller &agrave; part, &agrave; ce m&ecirc;me tarif vous pourrez trouver une filtration de puissance sup&eacute;rieure (voir dans la partie <a
href="http://www.construire-sa-piscine.com/BudgetPiscine.html">Budget</a>, je d&eacute;taille tous les postes).</p>
                <p>Un bloc polystyr&egrave;ne de 1m de long se vend en g&eacute;n&eacute;ral au prix public de 6&euro; le bloc (bien s&ucirc;r les prix peuvent varier suivant la densit&eacute; du bloc, suivant les remises, promos&hellip;). Dans le cas d&rsquo;une piscine de 10x5m de hauteur de mur constante de 1,25m (cinq hauteurs de blocs) on peut compter 150 blocs + 10% pour les chutes soit environ 165 blocs. A 6&euro; le bloc, cela fait un montant d&rsquo;environ 1000&euro;. Vous voyez qu'ensuite par rapport &agrave; un kit &agrave; 4000&euro;, il vous reste de la marge pour acheter pompe, filtre et divers &eacute;l&eacute;ments.</p>
                <p>Pour un bloc &agrave; bancher en b&eacute;ton, si on prend comme prix moyen 1,5&euro; le bloc (l&agrave;
                  encore les prix peuvent varier) on compte pour la m&ecirc;me piscine 360 blocs + 10%
                  pour les chutes soit environ 400 blocs. On a donc un co&ucirc;t de 600&euro;.</p>
                <p>On le voit, le bloc &agrave; bancher en b&eacute;ton reste le moins cher &agrave; mettre en &oelig;uvre.
                  Maintenant, n&rsquo;oubliez pas que dans le cas du bloc en b&eacute;ton, il faudra rajouter
                  le co&ucirc;t de l&rsquo;enduit (peut &ecirc;tre environ 300 &agrave; 400&euro;).</p>
                <p>Maintenant,c&rsquo;est le choix entre le prix plus bas et un prix un peu plus haut mais une
                  facilit&eacute; de pose accrue et plus rapide.</p>
