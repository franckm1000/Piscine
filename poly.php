<p>Je fais un chapitre &agrave; part sur ce type de blocs pour Piscine car m&ecirc;me si je ne les ai pas utilis&eacute;s et si ils ne conviennent pas pour le rev&ecirc;tement carrelage, leur mise en &oelig;uvre est tr&egrave;s proche de celle des blocs &agrave; bancher en b&eacute;ton. Aujourd&rsquo;hui, de plus en plus de personnes utilisent ce syst&egrave;me pour la construction de leur piscine,, il me semblait donc int&eacute;ressant d&rsquo;en parler ici.</p>
                <h2>A.Les diff&eacute;rences et similitudes entre blocs</h2>
                <p>Les blocs &agrave; bancher en b&eacute;ton standard ont une dimension de 20x20x50. En g&eacute;n&eacute;ral les blocs polystyr&egrave;nes pour Piscine ont une dimension sup&eacute;rieure, ils peuvent &ecirc;tre de 1m de long, 25cm de large et 25cm de haut. Cela est rendu possible du fait de leur faible poids. On a donc moins de blocs &agrave; assembler dans le cas du polystyr&egrave;ne.</p>
                
                <p>Comme les blocs &agrave; bancher b&eacute;ton, ils sont divis&eacute;s en alv&eacute;oles ce qui donnera apr&egrave;s montage des poches verticales dans lesquelles on remplira de b&eacute;ton.</p>
                <p>Le ferraillage se fait &agrave; l&rsquo;identique des blocs &agrave; bancher en b&eacute;ton.</p>
                <p>Les pi&egrave;ces &agrave; sceller de la Pisicne se posent de la m&ecirc;me fa&ccedil;on que sur les blocs traditionnels en faisant des r&eacute;servations.</p>
                <p>Un des gros avantages du bloc en polystyr&egrave;ne est sa l&eacute;g&egrave;ret&eacute; et sa facilit&eacute; de d&eacute;coupe.</p>
                <p>Les blocs en polystyr&egrave;nes ont une particularit&eacute;, c&rsquo;est leur densit&eacute; calcul&eacute;e en Kg/m3. En g&eacute;n&eacute;ral, elle se situe entre 25 et 30 Kg/m3. Plus la densit&eacute; est  &eacute;lev&eacute;e, plus le bloc est &laquo;&nbsp;solide&nbsp;&raquo;.</p>
                <p>Il semblerait que le bloc polystyr&egrave;ne ait un d&eacute;faut principal&nbsp;: il se d&eacute;formerait si on lui donne des coups &agrave; travers le liner (enfants jouant par exemple). Il faut alors rajouter entre le polystyr&egrave;ne et le liner de fines feuilles de PVC.</p>
                  <h2>B.Les diff&eacute;rents rev&ecirc;tements de Piscine</h2>
                <p>Une des limites du bloc en polystyr&egrave;ne est l&rsquo;obligation d&rsquo;opter pour un rev&ecirc;tementde Piscine type  liner ou PVC arm&eacute;&nbsp; (limite dans le sens limite de choix, pas dans le sens qualitatif !). En effet, il est impossible de coller du carrelage ou de faire un enduit sur le polystyr&egrave;ne (bien que&hellip;j&rsquo;ai entendu des choses l&agrave;-dessus&hellip;). </p>
