<p>Le terrassement de la piscine a commencé le Lundi 18 septembre 2006. J'ai fait appel à une entreprise près de chez moi pour le faire. Ils mettront deux jours et demi en tout pour y faire. Il faut savoir qu’en même temps je leur ai demandé d'autres travaux, assainissement des eaux usées notamment car le tuyau qui conduit à l’égout passait au milieu de la piscine.</p> 
<p>En outre, le terrain présente une architecture relativement accidentée. Ils se sont servis de la terre décaissée pour remblayer toute une partie près de l’escalier qui remonte à la maison. Terre qui ne suffira pas d’ailleurs, plus tard j’aurai besoin d’en faire venir pour remblayer autour de la piscine (beaucoup de remblai d’un côté car le terrain est en pente).</p>







<p>J'ai fais faire cette partie par un professionnel. Non pas que je n'aurais pas pu le faire moi m&ecirc;me, mais j'avais d'autres travaux &agrave; faire sur mon terrain (remblaiement, assainissement des eaux us&eacute;es, enrochement). Si vous d&eacute;cidez de  faire le terrassement de la piscine vous m&ecirc;me, il faudra vous procurer une mini-pelle ou ce qu'on appelle commun&eacute;ment un &quot;Godet&quot;. Je pense qu'on peut trouver des mini-pelles &agrave; environ 150&euro; la journ&eacute;e. Si vous comptez deux journ&eacute;es de travail pour y faire, on peut s'en tirer pour 300&euro; &agrave; faire le trou.</p>
<p>Remarque&nbsp;: Tracez bien les limites int&eacute;rieures de la <em><strong>piscine</strong></em>. Plantez quatre piquets en fer au quatre coins de la <em><strong>piscine</strong></em>. Faites en sorte que ces piquets d&eacute;passent une fois le radier coul&eacute; ils vous serviront de rep&egrave;re pour monter la premi&egrave;re ligne de blocs. Pensez &agrave; en mettre &eacute;galement aux quatre coins de l&#8217;escalier si vous en faites et sur les limites de la pointe de diamant. Comme sur ce sch&eacute;ma&nbsp;:</p>
