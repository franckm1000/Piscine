<p>Voici peut-&ecirc;tre une des parties sinon <b>LA</b> partie la plus compliqu&eacute;e de la construction de la <u><b>Piscine</b></u>. C&rsquo;est en tout cas celle qui m&rsquo;a inspir&eacute; le plus de crainte avant de commencer.</p>
<p>En effet, techniquement ces enduits sont loin d&rsquo;&ecirc;tre &eacute;vidents &agrave; faire. En outre, la surface &agrave; enduire est relativement importante (100m2 en comptant la chape au sol). Dans cette section, on ne parlera que des enduits aux murs, une section sp&eacute;ciale sera r&eacute;serv&eacute;e pour la chape au sol.</p>
<p>Pourquoi ces enduits? Parce que c&rsquo;est eux qui vont cr&eacute;er l&rsquo;<i><u>&eacute;tanch&eacute;it&eacute;</u></i> du bassin de la <b><u>Piscine</u></b>. En effet, contrairement &agrave; une id&eacute;e assez r&eacute;pandue, ce n&rsquo;est pas le carrelage qui cr&eacute;e l&rsquo;&eacute;tanch&eacute;it&eacute; du bassin mais les enduits  dessous. D&rsquo;o&ugrave; l&rsquo;importance vitale de les faire correctement.</p>
<h2>A.M&eacute;thode g&eacute;n&eacute;rale pour les enduits des Murs de la Piscine.</h2>
<p>On peut distinguer trois parties distinctes pour la r&eacute;alisation de cet enduit.</p>
<h3>i).Le gobetis d&rsquo;accrochage</h3>

<p>Les blocs en b&eacute;ton ne constituent pas une bonne accroche pour le futur enduit. Pour que l&rsquo;enduit accroche bien aux parois de la <em><b>Piscine</b></em> on fait un gobetis. Il s&rsquo;agit d&rsquo;un mortier gras (500kg de ciment par m3 de mortier) auquel on ajoute une r&eacute;sine d&rsquo;accrochage (type Sikalatex). Ce mortier est projet&eacute; sur les blocs en b&eacute;ton et liss&eacute; tr&egrave;s grossi&egrave;rement. Il est d&rsquo;une &eacute;paisseur faible (1 &agrave; 2mm maximum).</p>

<p>Une fois sec, il doit &ecirc;tre d&rsquo;une apparence noire et doit accrocher la peau quand on passe sa main dessus.</p>
<p>Le Sikalatex est une r&eacute;sine que l&rsquo;on rajoute au mortier et qui augmente l&rsquo;adh&eacute;rence de celui-ci. Il faut rajouter cette r&eacute;sine dans l&rsquo;eau au moment o&ugrave; l&rsquo;on g&acirc;che et pas une fois le mortier fait.</p>
