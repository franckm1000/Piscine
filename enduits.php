<h2>B.Technique pour r&eacute;aliser les enduits &eacute;tanches Piscine.</h2>
<p>Je donne ici quelques m&eacute;thodes que j&rsquo;ai utilis&eacute; pour r&eacute;aliser les enduits au mur de la <em><strong>Piscine</strong></em>.</p>
<h3>i).R&eacute;glage de l&rsquo;&eacute;paisseur de l&rsquo;enduit sur les murs de la piscine.</h3>
<p>Il faut commencer par d&eacute;terminer l&rsquo;&eacute;paisseur de l&rsquo;enduit. On a vu que celui-ci ferait 2cm (deux couches superpos&eacute;es de 1cm chacune). On va installer un syst&egrave;me nous permettant de mat&eacute;rialiser cette &eacute;paisseur.</p>
<p>On va prendre des planches de coffrage que l&rsquo;on va poser sur l&rsquo;arase de la <em><strong>piscine</strong></em>. On fera d&eacute;passer ces planches de 2cm &agrave; l&rsquo;int&eacute;rieur de la<em><strong> piscine</strong></em>. De cette sorte, en arrivant au ras de ces planches on aura 2cm d&rsquo;enduit.</p>
<p>Pour le premier enduit, on le fait &laquo;&nbsp;d&rsquo;environ&nbsp;&raquo; 1cm. C&rsquo;est au deuxi&egrave;me enduit qu&rsquo;on fait en sorte d&rsquo;arriver juste &agrave; limite de la planche.</p>
<p>Ce croquis pour mieux comprendre&nbsp;:</p>
