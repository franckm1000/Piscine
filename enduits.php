<p>Les planches seront chevill&eacute;es et fix&eacute;es sur l&rsquo;arase de la <em><strong>piscine</strong></em> de sorte qu&rsquo;elles ne puissent pas bouger.</p>
<p>Concernant les &eacute;l&eacute;ments &agrave; sceller, on a vu qu&rsquo;on avait laiss&eacute; une r&eacute;servation de 3cm. Une fois l&rsquo;enduit r&eacute;alis&eacute; chaque &eacute;l&eacute;ment doit donc encore d&eacute;passer d&rsquo;1 cm qui servira pour l&rsquo;&eacute;paisseur du carrelage.</p>
<h3>ii).Projeter et talocher l&rsquo;enduit piscine.</h3>
<p>Vous pouvez vous procurer par location une machine &agrave; projeter. Mais je me suis renseign&eacute; c&rsquo;est assez co&ucirc;teux la journ&eacute;e. N&rsquo;aimant pas &ecirc;tre prit par le temps j&rsquo;ai pr&eacute;f&eacute;r&eacute; y faire &agrave; la main.</p>
<p>Je vous assure que c&rsquo;est la premi&egrave;re fois de ma vie que j&rsquo;ai enduis un mur en projetant &agrave; la truelle. C&rsquo;est loin d&rsquo;&ecirc;tre &eacute;vident et il faut une p&eacute;riode plus ou moins longue d&rsquo;apprentissage.</p>
<p>Le plus important &agrave; ce moment l&agrave; est de <b><u>ne pas se d&eacute;courager</u></b>.</p>
<p>Pour ce qui est de talocher, idem il faut un certain temps mais si l&rsquo;enduit est bien appliqu&eacute; il n&rsquo;y a pas de probl&egrave;mes.</p>
<p>Voici quelques remarques&nbsp;:</p>
<p>-&nbsp;Essayer de travailler par temps sec, sans forte chaleur et sans vent si possible.</p>
<p>-&nbsp;<i><u>Humidifiez bien les murs de la <strong>piscine</strong></u></i> au jet avant de projeter votre enduit.</p>
<p>-&nbsp;Vu l&rsquo;&eacute;paisseur (1cm) vous pouvez commencer par enduire une faible &eacute;paisseur (quelques millim&egrave;tres par exemple) puis repasser peu de temps apr&egrave;s pour &eacute;paissir l&rsquo;enduit.</p>
<p>- Le mortier ne doit pas &ecirc;tre trop liquide mais pas trop &eacute;pais non plus. A vous de trouver le bon &eacute;quilibre (en g&eacute;n&eacute;ral &agrave; la deuxi&egrave;me ou troisi&egrave;me b&eacute;tonni&egrave;re).</p>
<p>- Une fois le mortier pr&ecirc;t dans la brouette, ne tra&icirc;nez pas trop pour le projeter car plus vous prenez de temps plus il durcit et plus l&rsquo;enduit est dur &agrave; faire tenir.</p>
<p>- Projetez sur une surface de 1 &agrave; 2 m&sup2; par exemple puis lissez un peu le mortier et ensuite talochez.</p>
<p>- Pour la premi&egrave;re couche, ce n&rsquo;est pas grave si elle n&rsquo;est pas parfaitement d&rsquo;aplomb.</p>
<p>- Pour la deuxi&egrave;me couche par contre, il faut que, l&rsquo;aplomb soit parfait. Pour cela, projetez assez &eacute;pais de fa&ccedil;on que le mortier d&eacute;passe de la planche (&eacute;paisseur finie de l&rsquo;enduit). Puis prenez une r&egrave;gle que vous posez verticalement le long du mur &agrave; niveau et vous vous d&eacute;placez alors le long du mur en enlevant le surplus de mortier. De cette fa&ccedil;on, l&rsquo;enduit est bien d&rsquo;aplomb. Talochez ensuite pour parfaire la finition.</p>
<p>-&nbsp;Essayez de travailler de l&rsquo;int&eacute;rieur de la <em><strong>piscine</strong></em> (sable, b&eacute;tonni&egrave;re, ciment&hellip;). De cette fa&ccedil;on, vous n&rsquo;avez pas &agrave; trimbaler sans arr&ecirc;t votre mortier.</p>
