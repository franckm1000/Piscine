<p>Cet hydrofuge se pr&eacute;sente soit sous forme liquide soit sous forme de poudre. C&rsquo;est cet hydrofuge qui va rendre le bassin &eacute;tanche. En effet, en mettant ce mortier hydrofuge partout, on rend le bassin de la<b> Piscine</b> &eacute;tanche.</p>
