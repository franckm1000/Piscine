<p class=MsoNormal align=center style='text-align:center'><u><span
style='font-size:16.0pt;font-family:"Comic Sans MS";color:#993366'>Le choix dé
béton&nbsp;:<o:p></o:p></span></u></p>

<p class=MsoNormal><span style='font-family:"Comic Sans MS"'><o:p>&nbsp;</o:p></span></p>

<ol style='margin-top:0cm' start=1 type=1>
 <li class=MsoNormal style='mso-list:l0 level1 lfo3;tab-stops:list 36.0pt'><span
     style='font-family:"Comic Sans MS"'>La norme pour faire un radier de
     piscine est de commander du béton <u>BPS C25/30</u>. Celui-ci est un béton
     standard de 350 kg/m3. C'est-à-dire que l’on a 350kg de ciment par m3 de
     béton.<o:p></o:p></span></li>
</ol>

<p class=MsoNormal style='margin-left:18.0pt'><span style='font-family:"Comic Sans MS"'><o:p>&nbsp;</o:p></span></p>

<ol style='margin-top:0cm' start=2 type=1>
 <li class=MsoNormal style='mso-list:l0 level1 lfo3;tab-stops:list 36.0pt'><span
     style='font-family:"Comic Sans MS"'>Je vous déconseille fortement
     d’utiliser du béton fibré. C’est vrai qu’il présente un avantage
     considérable&nbsp;: éviter de ferrailler, ce béton étant constitué de
     fines lamelles de fer. Mais il ne présente pas les garanties suffisantes
     pour un tel travail.<o:p></o:p></span></li>
</ol>

<p class=MsoNormal style='margin-left:18.0pt'><span style='font-family:"Comic Sans MS"'><o:p>&nbsp;</o:p></span></p>

<ol style='margin-top:0cm' start=3 type=1>
 <li class=MsoNormal style='mso-list:l0 level1 lfo3;tab-stops:list 36.0pt'><span
     style='font-family:"Comic Sans MS"'>Vous pouvez demander à ajouter au
     béton un adjuvant retardateur&nbsp;: cela retarde la prise du béton. Cela
     est valable quand il fait relativement chaud. De ce fait, si vous en avez
     la possibilité coulez le radier par température moyenne.<o:p></o:p></span></li>
</ol>

<p class=MsoNormal><span style='font-family:"Comic Sans MS"'><o:p>&nbsp;</o:p></span></p>

<p class=MsoNormal style='margin-left:18.0pt'><span style='font-family:"Comic Sans MS"'><o:p>&nbsp;</o:p></span></p>

<ol style='margin-top:0cm' start=4 type=1>
 <li class=MsoNormal style='mso-list:l0 level1 lfo3;tab-stops:list 36.0pt'><span
     style='font-family:"Comic Sans MS"'>Vous pouvez rajouter un adjuvant
     hydrofuge&nbsp;: cela permet de rendre le béton étanche. L’avantage est
     qu’avec ce procédé vous n’aurez plus à faire de chape hydrofuge. Mais cela
     n’est valable que si votre radier est parfaitement fini.<o:p></o:p></span></li>
</ol>
